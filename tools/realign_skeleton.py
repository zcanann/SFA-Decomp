#!/usr/bin/env python3
"""realign_skeleton.py — emit a v1.0-aligned C skeleton for a drifted unit.

Given a unit name, reads the matching .s in build/GSAE01/asm/, extracts the
asm function set (names + addresses + sizes), and prints a C skeleton with one
empty stub per asm function. The hunter then fills in bodies.

This implements the "add-new-function" / source-set restructure pattern
documented in CLAUDE.md and AGENTS.md, so a single drifted Ghidra-imported
.c can be brought into shape with one pass.

Usage:
  python3 tools/realign_skeleton.py main/dll/cannon            # to stdout
  python3 tools/realign_skeleton.py main/dll/cannon > new.c    # to file
  python3 tools/realign_skeleton.py main/dll/cannon --merge    # also keep existing src defs (commented)

The --merge mode preserves the existing .c content under a `#if 0 // LEGACY` block so the
old Ghidra-imported FUN_xxx bodies can be mined as you fill the stubs.
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
BUILD = REPO / "build" / "GSAE01"
SRC = REPO / "src"

ASM_FN_RE = re.compile(r"^\.fn\s+([A-Za-z_][A-Za-z0-9_]*)\s*,\s*(global|local)\s*$")
ASM_HDR_RE = re.compile(r"^#\s*\.text:0x[0-9A-Fa-f]+\s*\|\s*0x([0-9A-Fa-f]+)\s*\|\s*size:\s*0x([0-9A-Fa-f]+)")


def resolve_unit_name(query: str) -> str:
    """Resolve a user-supplied unit query (substring) against report.json.

    Accepts either the full unit name (`main/main/dll/cannonball`) or any
    unambiguous substring (`cannonball`, `dll/cannonball`).
    """
    report_path = BUILD / "report.json"
    if not report_path.is_file():
        sys.exit(f"missing {report_path}; run `ninja build/GSAE01/report.json` first")
    units = [u["name"] for u in json.loads(report_path.read_text())["units"]]
    if query in units:
        return query
    matches = [u for u in units if query in u]
    if not matches:
        sys.exit(f"no unit matches '{query}'")
    if len(matches) > 1:
        # If one is an exact prefix or path-component match, prefer it.
        exact = [u for u in matches if u.endswith("/" + query) or u == query]
        if len(exact) == 1:
            return exact[0]
        sys.exit(f"ambiguous '{query}'; matches: {', '.join(matches[:8])}{' ...' if len(matches) > 8 else ''}")
    return matches[0]


def parse_asm(path: Path) -> list[dict]:
    funcs: list[dict] = []
    pending_addr = pending_size = None
    for line in path.read_text(errors="replace").splitlines():
        m = ASM_HDR_RE.match(line)
        if m:
            pending_addr = int(m.group(1), 16)
            pending_size = int(m.group(2), 16)
            continue
        m = ASM_FN_RE.match(line)
        if m:
            funcs.append({
                "name": m.group(1),
                "addr": pending_addr,
                "size": pending_size,
                "linkage": m.group(2),
            })
            pending_addr = pending_size = None
    return funcs


def unit_source_path(unit_name: str) -> Path:
    parts = unit_name.split("/")
    if parts and parts[0] == "main":
        parts = parts[1:]
    return SRC / ("/".join(parts) + ".c")


def unit_asm_path(unit_name: str) -> Path:
    parts = unit_name.split("/")
    if parts and parts[0] == "main":
        parts = parts[1:]
    return BUILD / "asm" / ("/".join(parts) + ".s")


def header_for(unit_name: str) -> str:
    parts = unit_name.split("/")
    if parts and parts[0] == "main":
        parts = parts[1:]
    return "/".join(parts) + ".h"


def emit(unit_name: str, funcs: list[dict], merge_existing: str | None) -> str:
    out: list[str] = []
    rel_h = header_for(unit_name)
    out.append(f'/* SKELETON for {unit_name} — generated by tools/realign_skeleton.py')
    out.append(f' * Asm symbols: {len(funcs)}.  Fill bodies one at a time using:')
    out.append(f' *   python3 tools/function_objdump.py --diff {unit_name} <symbol>')
    out.append(' */')
    out.append('#include "types.h"')
    out.append(f'#include "{rel_h}"')
    out.append("")
    for f in funcs:
        addr_s = f"0x{f['addr']:08X}" if f["addr"] is not None else "?"
        size_s = f"0x{f['size']:X}" if f["size"] is not None else "?"
        out.append(f"/* {f['name']}  addr={addr_s}  size={size_s}  linkage={f['linkage']} */")
        # Provide a default void(void) stub; the hunter widens the signature once they see the asm.
        out.append(f"void {f['name']}(void) {{")
        out.append(f"  /* TODO: body — see build/GSAE01/asm/{unit_name.split('main/', 1)[-1]}.s */")
        out.append("}")
        out.append("")
    if merge_existing:
        out.append("/* ===== LEGACY src kept for mining; remove after restructure ===== */")
        out.append("#if 0")
        out.append(merge_existing.rstrip())
        out.append("#endif")
    return "\n".join(out) + "\n"


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("unit", help="unit name (e.g. main/dll/cannon)")
    p.add_argument("--merge", action="store_true", help="append existing src under #if 0 for mining")
    p.add_argument("--inplace", action="store_true", help="write to src path (DANGEROUS — overwrites)")
    args = p.parse_args()

    unit_name = resolve_unit_name(args.unit)
    s_path = unit_asm_path(unit_name)
    c_path = unit_source_path(unit_name)
    if not s_path.is_file():
        sys.exit(f"missing asm: {s_path}")
    funcs = parse_asm(s_path)
    if not funcs:
        sys.exit(f"no .fn directives found in {s_path}")

    merge_text = None
    if args.merge and c_path.is_file():
        merge_text = c_path.read_text(errors="replace")

    body = emit(unit_name, funcs, merge_text)
    if args.inplace:
        if not c_path.parent.is_dir():
            c_path.parent.mkdir(parents=True, exist_ok=True)
        c_path.write_text(body)
        print(f"wrote {c_path.relative_to(REPO)}", file=sys.stderr)
    else:
        sys.stdout.write(body)


if __name__ == "__main__":
    main()
