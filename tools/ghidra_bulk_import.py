def normalize(path: str) -> str:
    return path.replace("\\", "/")


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def strip_comments(text: str) -> str:
    text = re.sub(r"/\*.*?\*/", "", text, flags=re.S)
    text = re.sub(r"//.*", "", text)
    return text


def parse_splits(splits_path: Path) -> list[SplitSpan]:
    spans: list[SplitSpan] = []
    current_owner: str | None = None
    line_re = re.compile(r"start:0x([0-9A-Fa-f]+) end:0x([0-9A-Fa-f]+)")
    for raw_line in splits_path.read_text(encoding="utf-8").splitlines():
        if raw_line and not raw_line.startswith("\t") and raw_line.endswith(":"):
            current_owner = raw_line[:-1]
            continue
        if current_owner is None:
            continue
        line = raw_line.strip()
        if not line.startswith(".text"):
            continue
        match = line_re.search(line)
        if match is None:
            continue
        spans.append(SplitSpan(current_owner, int(match.group(1), 16), int(match.group(2), 16)))
    return spans


def owner_for_address(spans: Iterable[SplitSpan], address: int) -> SplitSpan | None:
    for span in spans:
        if span.start <= address < span.end:
            return span
    return None


def should_manage_existing(path: Path, overwrite_existing: bool) -> bool:
    if not path.exists():
        return True
    if overwrite_existing:
        return True
    head = path.read_text(encoding="utf-8", errors="replace")[:1024]
    return (
        GENERATED_MARKER in head
        or CLAIM_AUTO_MARKER in head
        or SOURCE_MATERIALIZE_MARKER in head
        or (NON_BUILT_STUB_MARKER in head and "#include" not in head)
    )


def parse_signature_block(raw_text: str, name: str) -> tuple[str, str]:
    lines = raw_text.splitlines()
    target_line = None
    for index, line in enumerate(lines):
        if re.search(rf"\b{name}\s*\(", line):
            target_line = index
            break
    if target_line is None:
        raise ValueError(f"Could not find signature for {name}")

    start = target_line
    while start > 0:
        previous = lines[start - 1].strip()
        if not previous or previous.startswith("//") or previous.startswith("/*"):
            break
        start -= 1

    collected: list[str] = []
    depth = 0
    seen_open = False
    for line in lines[start:]:
        stripped = line.rstrip()
        if stripped.strip() == "{":
            break
        collected.append(stripped)
        depth += stripped.count("(") - stripped.count(")")
        if "(" in stripped:
            seen_open = True
        if seen_open and depth <= 0 and ")" in stripped:
            break

    signature_text = "\n".join(collected).strip()
    name_index = signature_text.find(name)
    return_type = signature_text[:name_index].strip() if name_index != -1 else "undefined4"
    if not return_type:
        return_type = "undefined4"
    return signature_text, return_type


def simplify_return_type(return_type: str) -> str:
    lowered = return_type.replace("\n", " ").strip()
    if "*" in lowered:
        return "void*"
    if lowered == "void":
        return "void"
    if "double" in lowered or lowered == "f64":
        return "double"
    if "float" in lowered or lowered == "f32":
        return "float"
    if any(token in lowered for token in ("int", "short", "char", "long", "s8", "s16", "s32", "s64", "u8", "u16", "u32", "u64", "uint", "ulong", "undefined", "byte", "bool")):
        return "undefined4"
    return "undefined4"


def make_safe_forward_declaration(function: FunctionDump) -> str:
    if function.safe_signature:
        return function.signature_text + ";"
    return f"{simplify_return_type(function.return_type)} {function.name}();"


def make_loose_forward_declaration(function: FunctionDump) -> str:
    return f"{normalize_type_name(function.return_type)} {function.name}();"


def default_return_statement(return_type: str) -> str | None:
    simplified = simplify_return_type(return_type)
    if simplified == "void":
        return None
    if simplified == "double":
        return "    return 0.0;"
    if simplified == "float":
        return "    return 0.0f;"
    return "    return 0;"


def detect_calls(raw_text: str, current_name: str) -> list[str]:
    raw_text = strip_comments(raw_text)
    calls: set[str] = set()
    for match in CALL_RE.finditer(raw_text):
        token = match.group(1)
        if token == current_name:
            continue
        if token in CONTROL_TOKENS or token in SUPPORTED_HELPERS or token in KNOWN_TYPES:
            continue
        if token.startswith("LAB_"):
            continue
        calls.add(token)
    for token in FUNCTION_REF_RE.findall(raw_text):
        if token != current_name:
            calls.add(token)
    return sorted(calls)


def detect_globals(raw_text: str) -> list[str]:
    raw_text = strip_comments(raw_text)
    return sorted(set(GLOBAL_RE.findall(raw_text)))


def normalize_type_name(type_name: str) -> str:
    compact = " ".join(type_name.replace("\n", " ").split())
    compact = compact.replace(" *", "*")
    return compact


def pointer_base_type(type_name: str) -> str | None:
    normalized = normalize_type_name(type_name)
    if not normalized.endswith("*"):
        return None
    return normalized[:-1].rstrip()


def parse_local_var_types(raw_text: str) -> dict[str, str]:
    if "{" not in raw_text:
        return {}
    body = raw_text.split("{", 1)[1]
    var_types: dict[str, str] = {}
    for raw_line in body.splitlines():
        line = raw_line.strip()
        if not line or line.startswith("//") or line.startswith("/*") or "(" in line:
            continue
        match = LOCAL_DECL_RE.match(line)
        if match is None:
            continue
        type_name = match.group("type")
        type_tokens = type_name.split()
        if not type_tokens or any(token not in KNOWN_TYPES for token in type_tokens):
            continue
        pointer = match.group("pointer") or ""
        var_types[match.group("name")] = normalize_type_name(f"{type_name}{pointer}")
    return var_types


def observe_symbol_type(
    inferred: dict[str, str],
    symbol: str,
    type_name: str,
) -> bool:
    normalized = normalize_type_name(type_name)
    if not normalized:
        return False
    current = inferred.get(symbol)
    if current == normalized:
        return False

    def score(name: str) -> tuple[int, int]:
        pointer = 1 if pointer_base_type(name) is not None else 0
        specific = 0 if name in {"undefined4", "void*"} else 1
        return (pointer, specific)

    if current is None or score(normalized) > score(current):
        inferred[symbol] = normalized
        return True
    return False


def infer_global_types(functions: list[FunctionDump]) -> dict[str, str]:
    inferred: dict[str, str] = {}
    local_var_maps = {function.name: parse_local_var_types(function.raw_text) for function in functions}
    stripped_sources = {function.name: strip_comments(function.raw_text) for function in functions}
    all_symbols = {symbol for function in functions for symbol in function.globals_used}

    for function in functions:
        raw_text = stripped_sources[function.name]
        local_var_types = local_var_maps[function.name]
        return_base = pointer_base_type(function.return_type)
        for var_name, var_type in local_var_types.items():
            escaped_var = re.escape(var_name)
            base_type = pointer_base_type(var_type)
            if base_type is None:
                continue
            for symbol in function.globals_used:
                escaped_symbol = re.escape(symbol)
                if re.search(rf"\b{escaped_var}\s*=\s*{escaped_symbol}\b", raw_text):
                    observe_symbol_type(inferred, symbol, var_type)
                if re.search(rf"\b{escaped_var}\s*=\s*&{escaped_symbol}\b", raw_text):
                    observe_symbol_type(inferred, symbol, base_type)
        if return_base is not None:
            for symbol in function.globals_used:
                escaped_symbol = re.escape(symbol)
                if re.search(rf"\breturn\s+{escaped_symbol}\b", raw_text):
                    observe_symbol_type(inferred, symbol, function.return_type)
                if re.search(rf"\b{escaped_symbol}\s*[!=]=\s*\({re.escape(return_base)}\s*\*\)\s*0x0", raw_text):
                    observe_symbol_type(inferred, symbol, function.return_type)

    changed = True
    while changed:
        changed = False
        for function in functions:
            raw_text = stripped_sources[function.name]
            for lhs, rhs in re.findall(r"\b([A-Za-z_]\w*)\s*=\s*&([A-Za-z_]\w+)\b", raw_text):
                if lhs not in all_symbols or rhs not in all_symbols:
                    continue
                rhs_type = inferred.get(rhs)
                lhs_type = inferred.get(lhs)
                if rhs_type is not None:
                    changed |= observe_symbol_type(inferred, lhs, f"{rhs_type} *")
                else:
                    changed |= observe_symbol_type(inferred, lhs, "undefined4 *")
                if lhs_type is not None:
                    lhs_base = pointer_base_type(lhs_type)
                    if lhs_base is not None:
                        changed |= observe_symbol_type(inferred, rhs, lhs_base)
            for lhs, rhs in re.findall(r"\b([A-Za-z_]\w*)\s*=\s*([A-Za-z_]\w+)\b", raw_text):
                if lhs not in all_symbols or rhs not in all_symbols:
                    continue
                lhs_type = inferred.get(lhs)
                rhs_type = inferred.get(rhs)
                if lhs_type is not None and pointer_base_type(lhs_type) is not None:
                    changed |= observe_symbol_type(inferred, rhs, lhs_type)
                if rhs_type is not None and pointer_base_type(rhs_type) is not None:
                    changed |= observe_symbol_type(inferred, lhs, rhs_type)

    for function in functions:
        raw_text = stripped_sources[function.name]
        for symbol in function.globals_used:
            escaped_symbol = re.escape(symbol)
            if re.search(rf"(?<![A-Za-z0-9_])\*{escaped_symbol}\b", raw_text):
                observe_symbol_type(inferred, symbol, "undefined4 *")
            if re.search(rf"\b{escaped_symbol}\s*\[", raw_text):
                observe_symbol_type(inferred, symbol, "undefined4 *")

    for symbol in list(inferred):
        if symbol.startswith("FLOAT_"):
            inferred[symbol] = "f32"
        elif symbol.startswith("DOUBLE_"):
            inferred[symbol] = "f64"

    return inferred


def has_pointer_assignment_hazard(raw_text: str) -> bool:
    stripped = strip_comments(raw_text)
    local_var_types = parse_local_var_types(raw_text)
    pointer_locals = [
        name for name, type_name in local_var_types.items() if pointer_base_type(type_name) is not None
    ]
    for lhs in pointer_locals:
        escaped_lhs = re.escape(lhs)
        for rhs in pointer_locals:
            escaped_rhs = re.escape(rhs)
            if re.search(rf"\b{escaped_lhs}\s*\[[^\]]+\]\s*=\s*{escaped_rhs}\b", stripped):
                return True
            if re.search(rf"\*\s*{escaped_lhs}\s*=\s*{escaped_rhs}\b", stripped):
                return True
    return False


def detect_conflicted_pointer_globals(functions: list[FunctionDump]) -> set[str]:
    pointer_bases: dict[str, set[str]] = {}
    for function in functions:
        raw_text = strip_comments(function.raw_text)
        local_var_types = parse_local_var_types(function.raw_text)
        for var_name, var_type in local_var_types.items():
            base_type = pointer_base_type(var_type)
            if base_type is None:
                continue
            escaped_var = re.escape(var_name)
            for symbol in function.globals_used:
                escaped_symbol = re.escape(symbol)
                if re.search(rf"\b{escaped_var}\s*=\s*{escaped_symbol}\b", raw_text):
                    pointer_bases.setdefault(symbol, set()).add(base_type)
    return {symbol for symbol, bases in pointer_bases.items() if len(bases) > 1}


def detect_void_return_assignment_hazards(functions: list[FunctionDump]) -> dict[str, list[str]]:
    hazards: dict[str, list[str]] = {}
    local_return_types = {function.name: simplify_return_type(function.return_type) for function in functions}
    stripped_sources = {function.name: strip_comments(function.raw_text) for function in functions}

    for function in functions:
        raw_text = stripped_sources[function.name]
        bad_callees: list[str] = []
        for callee, return_type in local_return_types.items():
            if callee == function.name or return_type != "void":
                continue
            escaped_callee = re.escape(callee)
            if re.search(rf"(?<![=!<>+\-*/%&|^])=\s*{escaped_callee}\s*\(", raw_text):
                bad_callees.append(callee)
                continue
            if re.search(rf"(?<![=!<>+\-*/%&|^])=\s*\([^()]+\)\s*{escaped_callee}\s*\(", raw_text):
                bad_callees.append(callee)
                continue
            if simplify_return_type(function.return_type) != "void" and re.search(
                rf"\breturn\s+{escaped_callee}\s*\(",
                raw_text,
            ):
                bad_callees.append(callee)
                continue
            if simplify_return_type(function.return_type) != "void" and re.search(
                rf"\breturn\s+\([^()]+\)\s*{escaped_callee}\s*\(",
                raw_text,
            ):
                bad_callees.append(callee)
        if bad_callees:
            hazards[function.name] = sorted(set(bad_callees))

    return hazards


def function_stub_reasons(
    owner: str,
    functions: list[FunctionDump],
    forced_stub_owners: set[str] | None = None,
) -> dict[str, list[str]]:
    reasons: dict[str, list[str]] = {}
    conflicted_globals = detect_conflicted_pointer_globals(functions)
    void_assignment_hazards = detect_void_return_assignment_hazards(functions)
    forced_stub_owners = forced_stub_owners or set()
    for function in functions:
        function_reasons = list(function.unsafe_reasons)
        if owner in FORCE_STUB_OWNERS or owner in forced_stub_owners:
            function_reasons.append("forced full-owner stub for compile-first import")
        if function.name in FORCE_STUB_FUNCTIONS:
            function_reasons.append("forced stub for compile-first import")
        conflicted_used = sorted(conflicted_globals.intersection(function.globals_used))
        if conflicted_used:
            function_reasons.append(
                "conflicting local pointer bases for shared globals: "
                + ", ".join(conflicted_used)
            )
        bad_void_callees = void_assignment_hazards.get(function.name)
        if bad_void_callees:
            function_reasons.append(
                "uses return value from void local helper(s): " + ", ".join(bad_void_callees)
            )
        if function_reasons:
            reasons[function.name] = function_reasons
    return reasons


def observe_external_return_type(
    inferred: dict[str, str],
    name: str,
    type_name: str,
) -> None:
    normalized = normalize_type_name(type_name)
    current = inferred.get(name)
    if current == normalized:
        return

    def score(candidate: str) -> tuple[int, int]:
        return (1 if pointer_base_type(candidate) is not None else 0, 0 if candidate == "undefined4" else 1)

    if current is None or score(normalized) > score(current):
        inferred[name] = normalized


def infer_external_function_types(functions: list[FunctionDump], local_names: set[str]) -> dict[str, str]:
    inferred: dict[str, str] = {}
    local_var_maps = {function.name: parse_local_var_types(function.raw_text) for function in functions}
    stripped_sources = {function.name: strip_comments(function.raw_text) for function in functions}

    for function in functions:
        raw_text = stripped_sources[function.name]
        local_var_types = local_var_maps[function.name]
        calls = [call for call in function.called_functions if call not in local_names]
        for call in calls:
            escaped_call = re.escape(call)
            for var_name, var_type in local_var_types.items():
                escaped_var = re.escape(var_name)
                if re.search(rf"\b{escaped_var}\s*=\s*{escaped_call}\s*\(", raw_text):
                    observe_external_return_type(inferred, call, var_type)
            simplified_return = simplify_return_type(function.return_type)
            if re.search(rf"\breturn\s+{escaped_call}\s*\(", raw_text):
                observe_external_return_type(inferred, call, simplified_return)

    return inferred


def emit_external_function_return_type(type_name: str) -> str:
    if pointer_base_type(type_name) is not None:
        return "void*"
    return type_name


def check_safety(raw_text: str, signature_text: str) -> tuple[bool, bool, list[str]]:
    raw_text = strip_comments(raw_text)
    reasons: list[str] = []
    safe_signature = True
    safe_body = True

    if UNSUPPORTED_TOKEN_RE.search(signature_text):
        reasons.append("unsupported signature pseudo-types")
        safe_signature = False

    if UNSUPPORTED_TOKEN_RE.search(raw_text):
        reasons.append("unsupported body pseudo-types")
        safe_body = False

    if INVALID_SLICE_RE.search(raw_text):
        reasons.append("invalid Ghidra slice syntax")
        safe_body = False

    if LABEL_REF_RE.search(raw_text):
        reasons.append("address-label references need manual cleanup")
        safe_body = False

    if CPP_SCOPE_RE.search(raw_text):
        reasons.append("C++-style scope tokens need manual cleanup")
        safe_body = False

    if STRING_LABEL_RE.search(raw_text):
        reasons.append("raw string-label references need manual cleanup")
        safe_body = False

    if GLOBAL_ADDRESS_RE.search(raw_text):
        reasons.append("address-of global symbols need manual typing")
        safe_body = False

    if UNDECLARED_LOCAL_RE.search(raw_text):
        reasons.append("undeclared wide temporaries need manual cleanup")
        safe_body = False

    if TYPED_NULL_COMPARE_RE.search(raw_text):
        reasons.append("typed global pointer comparisons need manual cleanup")
        safe_body = False

    if re.search(r"\bNAN\b", raw_text):
        reasons.append("NAN macro needs manual cleanup")
        safe_body = False

    unsupported_helpers = {
        helper
        for helper in HELPER_TOKEN_RE.findall(raw_text)
        if helper not in SUPPORTED_HELPERS
    }
    if unsupported_helpers:
        reasons.append(f"unsupported helper macros: {', '.join(sorted(unsupported_helpers))}")
        safe_body = False

    if has_pointer_assignment_hazard(raw_text):
        reasons.append("pointer-heavy local typing needs manual cleanup")
        safe_body = False

    if FUNCTION_PTR_ASSIGN_RE.search(raw_text) or GLOBAL_FUNCTION_ASSIGN_RE.search(raw_text):
        reasons.append("function-pointer assignments need manual cleanup")
        safe_body = False

    if SCALAR_MEMBER_TO_POINTER_CAST_RE.search(raw_text):
        reasons.append("scalar-to-pointer casts need manual cleanup")
        safe_body = False

    if DEREF_INT_TO_POINTER_CAST_RE.search(raw_text):
        reasons.append("int-to-pointer casts of dereferenced values need manual cleanup")
        safe_body = False

    return safe_signature, safe_body, reasons


def load_function_dump(path: Path) -> FunctionDump:
    match = GHIDRA_FILE_RE.match(path.name)
    if match is None:
        raise ValueError(f"Unexpected Ghidra dump filename: {path.name}")
    raw_text = path.read_text(encoding="utf-8")
    name = match.group("name")
    signature_text, return_type = parse_signature_block(raw_text, name)
    safe_signature, safe_body, unsafe_reasons = check_safety(raw_text, signature_text)
    return FunctionDump(
        address=int(match.group("addr"), 16),
        size=0,
        name=name,
        raw_text=raw_text.rstrip() + "\n",
        signature_text=signature_text,
        return_type=return_type,
        safe_signature=safe_signature,
        safe_body=safe_body,
        unsafe_reasons=unsafe_reasons,
        called_functions=detect_calls(raw_text, name),
        globals_used=detect_globals(raw_text),
    )


def extern_decl_for_global(symbol: str, inferred_type: str | None = None) -> str:
    if inferred_type is not None:
        return f"extern {inferred_type} {symbol};"
    addr_match = re.search(r"([0-9a-fA-F]+)$", symbol)
    addr = int(addr_match.group(1), 16) if addr_match is not None else None
    if symbol.startswith("s_"):
        return f"extern char {symbol}[];"
    if symbol.startswith("DOUBLE_"):
        return f"extern f64 {symbol};"
    if symbol.startswith("FLOAT_"):
        return f"extern f32 {symbol};"
    if symbol.startswith("PTR_"):
        return f"extern void* {symbol};"
    if symbol.startswith("psRam"):
        return f"extern short* {symbol};"
    if symbol.startswith("piRam"):
        return f"extern int* {symbol};"
    if symbol.startswith("pfRam"):
        return f"extern f32* {symbol};"
    if symbol.startswith("fRam"):
        return f"extern f32 {symbol};"
    if symbol.startswith("iRam") and addr is not None and addr % 4 == 0:
        return f"extern int {symbol};"
    if symbol.startswith("sRam") and addr is not None and addr % 2 == 0:
        return f"extern short {symbol};"
    if symbol.startswith(("bRam", "cRam", "uRam")):
        if addr is not None and addr % 4 == 0:
            return f"extern undefined4 {symbol};"
        if addr is not None and addr % 2 == 0:
            return f"extern undefined2 {symbol};"
        return f"extern undefined {symbol};"
    return f"extern undefined4 {symbol};"


def render_function_info_block(function: FunctionDump) -> str:
    lines = [
        "/*",
        " * --INFO--",
        " *",
        f" * EN v1.0 Address: 0x{function.address:08X}",
        f" * EN v1.0 Size: {function.size}b",
        " * EN v1.1 Address: TODO",
        " * EN v1.1 Size: TODO",
        " * JP Address: TODO",
        " * JP Size: TODO",
        " * PAL Address: TODO",
        " * PAL Size: TODO",
        " */",
    ]
    return "\n".join(lines) + "\n"


def strip_leading_function_prologue(raw_text: str) -> str:
    lines = raw_text.rstrip().splitlines()
    index = 0

    while index < len(lines):
        stripped = lines[index].strip()
        if not stripped:
            index += 1
            continue
        if stripped.startswith("//"):
            index += 1
            continue
        if stripped.startswith("/*"):
            while index < len(lines):
                end_stripped = lines[index].strip()
                index += 1
                if "*/" in end_stripped:
                    break
            continue
        break

    return "\n".join(lines[index:]).rstrip() + "\n"


def normalize_null_pointer_casts(raw_text: str, inferred_global_types: dict[str, str]) -> str:
    normalized = raw_text
    for symbol, type_name in inferred_global_types.items():
        if pointer_base_type(type_name) is None:
            continue
        escaped_symbol = re.escape(symbol)
        normalized_type = normalize_type_name(type_name)
        normalized = re.sub(
            rf"(\b{escaped_symbol}\s*=\s*)\([^()]+\*\)\s*0x0\b",
            rf"\1({normalized_type})0x0",
            normalized,
        )
    return normalized


def header_relpath_for_owner(owner: str) -> str:
    return normalize(owner).removesuffix(".c") + ".h"


def header_guard(relpath: str) -> str:
    return re.sub(r"[^A-Za-z0-9]", "_", relpath.upper()) + "_"


def render_stub(function: FunctionDump, override_reasons: list[str] | None = None) -> str:
    lines: list[str] = []
    if function.safe_signature:
        lines.append(function.signature_text)
    else:
        lines.append(f"{simplify_return_type(function.return_type)}")
        lines.append(f"{function.name}()")
    lines.append("{")
    return_stmt = default_return_statement(function.return_type)
    if return_stmt is not None:
        lines.append(return_stmt)
    lines.append("}")
    return "\n".join(lines) + "\n"


def render_source(
    owner: str,
    span: SplitSpan,
    functions: list[FunctionDump],
    emit_headers: bool,
    forced_stub_owners: set[str] | None = None,
) -> str:
    local_names = {function.name for function in functions}
    extern_functions = sorted({call for function in functions for call in function.called_functions if call not in local_names})
    extern_globals = sorted({symbol for function in functions for symbol in function.globals_used})
    inferred_global_types = infer_global_types(functions)
    inferred_external_function_types = infer_external_function_types(functions, local_names)
    stub_reasons = function_stub_reasons(owner, functions, forced_stub_owners)
    safe_count = len(functions) - len(stub_reasons)
    stubbed_count = len(stub_reasons)

    lines: list[str] = [
        "/*",
        f" * {GENERATED_MARKER}",
        " *",
        f" * Owner: {owner}",
        f" * Text span: 0x{span.start:08X}-0x{span.end:08X}",
        f" * Imported Ghidra functions: {len(functions)}",
        f" * Verbatim-safe functions: {safe_count}",
        f" * Auto-stubbed functions: {stubbed_count}",
        " */",
        "",
        '#include "ghidra_import.h"',
    ]
    if emit_headers:
        lines.append(f'#include "{header_relpath_for_owner(owner)}"')
    lines.append("")

    if extern_functions:
        lines.append("/* Cross-file calls lifted from the raw Ghidra output. */")
        for name in extern_functions:
            return_type = emit_external_function_return_type(
                inferred_external_function_types.get(name, "undefined4")
            )
            lines.append(f"extern {return_type} {name}();")
        lines.append("")

    if extern_globals:
        lines.append("/* Raw global references kept as loose externs for later cleanup. */")
        for symbol in extern_globals:
            lines.append(extern_decl_for_global(symbol, inferred_global_types.get(symbol)))
        lines.append("")

    lines.append("/* Local declarations keep imported functions visible within the TU. */")
    for function in functions:
        lines.append(make_safe_forward_declaration(function))
    lines.append("")

    for function in functions:
        reasons = stub_reasons.get(function.name)
        lines.append(render_function_info_block(function).rstrip())
        if reasons is None:
            body_text = normalize_null_pointer_casts(function.raw_text, inferred_global_types)
            lines.append(strip_leading_function_prologue(body_text).rstrip())
        else:
            lines.append(render_stub(function, reasons).rstrip())
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_header(owner: str, functions: list[FunctionDump]) -> str:
    relpath = header_relpath_for_owner(owner)
    guard = header_guard(relpath)
    lines = [
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        '#include "ghidra_import.h"',
        "",
    ]
    for function in functions:
        lines.append(make_safe_forward_declaration(function))
    lines.extend(["", f"#endif /* {guard} */", ""])
    return "\n".join(lines)


def parse_inventory_functions(source_path: Path) -> list[InventoryFunction]:
    if not source_path.exists():
        return []
    entries: list[InventoryFunction] = []
    pattern = re.compile(
        r"^\s*\*\s*-\s*(?P<name>[A-Za-z_]\w*)\s+@\s+0x(?P<addr>[0-9A-Fa-f]+)\s+size\s+0x(?P<size>[0-9A-Fa-f]+)\s*$"
    )
    for line in source_path.read_text(encoding="utf-8", errors="replace").splitlines():
        match = pattern.match(line)
        if match is None:
            continue
        entries.append(
            InventoryFunction(
                address=int(match.group("addr"), 16),
                name=match.group("name"),
                size=int(match.group("size"), 16),
            )
        )
    entries.sort(key=lambda function: function.address)
    return entries


def render_inventory_source(
    owner: str,
    span: SplitSpan,
    inventory: list[InventoryFunction],
    emit_headers: bool,
) -> str:
    lines: list[str] = [
        "/*",
        f" * {GENERATED_MARKER}",
        " *",
        f" * Owner: {owner}",
        f" * Text span: 0x{span.start:08X}-0x{span.end:08X}",
        " * Imported Ghidra functions: 0",
        f" * Inventory-derived stub functions: {len(inventory)}",
        " */",
        "",
        '#include "ghidra_import.h"',
    ]
    if emit_headers:
        lines.append(f'#include "{header_relpath_for_owner(owner)}"')
    lines.extend(["", "/* No raw Ghidra dump was available for this owner yet. */"])
    lines.append("/* Split inventory stubs keep the file compiled and routable in the meantime. */")
    lines.append("")

    for function in inventory:
        lines.append(f"undefined4 {function.name}(void);")
    lines.append("")

    for function in inventory:
        lines.extend(
            [
                f"/* Function: {function.name} */",
                f"/* Entry: 0x{function.address:08X} */",
                f"/* Size: 0x{function.size:X} */",
                "undefined4",
                f"{function.name}(void)",
                "{",
                "    return 0;",
                "}",
                "",
            ]
        )

    return "\n".join(lines).rstrip() + "\n"


