from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path


GENERATED_MARKER = "Auto-generated by tools/claim_auto_game_splits.py."
DEFAULT_REPORT_PATH = Path("build/GSAE01/report.json")
DEFAULT_SPLITS_PATH = Path("config/GSAE01/splits.txt")
DEFAULT_SRC_ROOT = Path("src")
DEFAULT_INCLUDE_ROOT = Path("include")
DEFAULT_PLACEHOLDER_ROOT = "main/unknown/autos"


@dataclass(frozen=True)
class AutoTextUnit:
    name: str
    start: int
    end: int
    size: int
    functions: tuple[tuple[str, int, int], ...]
    placeholder_relpath: str
    previous_named: str | None
    next_named: str | None


@dataclass
class SplitBlock:
    start_index: int
    end_index: int
    path: str
    text_start: int | None


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def normalize(path: str) -> str:
    return path.replace("\\", "/")


def placeholder_relpath(root: str, start: int) -> str:
    return f"{root}/placeholder_{start:08X}.c"


def parse_report_units(report_path: Path, placeholder_root: str) -> list[AutoTextUnit]:
    report = json.loads(report_path.read_text(encoding="utf-8"))
    units = report["units"]

    text_windows: list[tuple[int, int, str, bool]] = []
    for unit in units:
        metadata = unit.get("metadata", {})
        if metadata.get("module_name") != "main":
            continue
        sections = unit.get("sections", [])
        text_section = next((section for section in sections if section["name"] == ".text"), None)
        if text_section is None:
            continue
        start = int(text_section["metadata"]["virtual_address"])
        end = start + int(text_section["size"])
        text_windows.append((start, end, unit["name"], metadata.get("auto_generated", False)))

    text_windows.sort()

    auto_units: list[AutoTextUnit] = []
    for unit in units:
        metadata = unit.get("metadata", {})
        name = unit.get("name", "")
        if metadata.get("module_name") != "main" or not metadata.get("auto_generated", False):
            continue
        if not isinstance(name, str) or not name.startswith("main/auto_03_"):
            continue

        text_section = next((section for section in unit.get("sections", []) if section["name"] == ".text"), None)
        if text_section is None:
            continue
        start = int(text_section["metadata"]["virtual_address"])
        size = int(text_section["size"])
        end = start + size

        previous_named = None
        next_named = None
        for other_start, other_end, other_name, other_auto in text_windows:
            if other_end <= start and not other_auto:
                previous_named = other_name
            if other_start >= end and not other_auto:
                next_named = other_name
                break

        functions = tuple(
            (
                function["name"],
                int(function["size"]),
                int(function["metadata"]["virtual_address"]),
            )
            for function in unit.get("functions", [])
        )
        auto_units.append(
            AutoTextUnit(
                name=name,
                start=start,
                end=end,
                size=size,
                functions=functions,
                placeholder_relpath=placeholder_relpath(placeholder_root, start),
                previous_named=previous_named,
                next_named=next_named,
            )
        )

    auto_units.sort(key=lambda unit: unit.start)
    return auto_units


def parse_split_blocks(splits_path: Path) -> tuple[list[str], list[SplitBlock]]:
    lines = splits_path.read_text(encoding="utf-8").splitlines(keepends=True)
    blocks: list[SplitBlock] = []
    current_path: str | None = None
    current_start = 0
    text_start: int | None = None

    for index, line in enumerate(lines):
        if line and not line.startswith("\t") and line.rstrip().endswith(":"):
            if current_path is not None:
                blocks.append(SplitBlock(current_start, index, current_path, text_start))
            current_path = line.rstrip()[:-1]
            current_start = index
            text_start = None
            continue
        if current_path is None:
            continue
        stripped = line.strip()
        if stripped.startswith(".text"):
            match = re.search(r"start:0x([0-9A-Fa-f]+)", stripped)
            if match is not None:
                text_start = int(match.group(1), 16)

    if current_path is not None:
        blocks.append(SplitBlock(current_start, len(lines), current_path, text_start))
    return lines, blocks


def update_splits(splits_path: Path, auto_units: list[AutoTextUnit]) -> int:
    lines, blocks = parse_split_blocks(splits_path)
    existing_paths = {block.path for block in blocks}
    existing_starts = {block.text_start for block in blocks if block.text_start is not None}

    insertions: list[tuple[int, str]] = []
    for unit in auto_units:
        if unit.placeholder_relpath in existing_paths or unit.start in existing_starts:
            continue

        insert_index = len(lines)
        for block in blocks:
            if block.text_start is not None and block.text_start > unit.start:
                insert_index = block.start_index
                break

        block_text = (
            f"{unit.placeholder_relpath}:\n"
            f"\t.text       start:0x{unit.start:08X} end:0x{unit.end:08X}\n\n"
        )
        insertions.append((insert_index, block_text))

    if not insertions:
        return 0

    for insert_index, block_text in sorted(insertions, key=lambda item: item[0], reverse=True):
        lines.insert(insert_index, block_text)

    splits_path.write_text("".join(lines), encoding="utf-8", newline="\n")
    return len(insertions)


def header_guard(relpath: str) -> str:
    return re.sub(r"[^A-Za-z0-9]", "_", relpath.upper()) + "_"


def render_header(unit: AutoTextUnit) -> str:
    relpath = unit.placeholder_relpath.removesuffix(".c") + ".h"
    guard = header_guard(relpath)
    lines = [
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "/*",
        f" * {GENERATED_MARKER}",
        " *",
        f" * Placeholder owner for the former auto-generated game text window 0x{unit.start:08X}-0x{unit.end:08X}.",
        f" * Previous named split: {unit.previous_named or '-'}",
        f" * Next named split: {unit.next_named or '-'}",
        f" * Function count: {len(unit.functions)}",
        " *",
        " * Function inventory:",
    ]
    for name, size, address in unit.functions:
        lines.append(f" * - {name} @ 0x{address:08X} size 0x{size:X}")
    lines.extend([" */", "", f"#endif /* {guard} */", ""])
    return "\n".join(lines)


def render_source(unit: AutoTextUnit) -> str:
    header_relpath = unit.placeholder_relpath.removesuffix(".c") + ".h"
    lines = [
        "/*",
        f" * {GENERATED_MARKER}",
        " *",
        " * This file claims a previously auto-generated game-code text window so it has a stable home",
        " * in `splits.txt` and can be subdivided into real sources later.",
        " *",
        f" * Current EN split: 0x{unit.start:08X}-0x{unit.end:08X}",
        f" * Previous named split: {unit.previous_named or '-'}",
        f" * Next named split: {unit.next_named or '-'}",
        f" * Raw auto unit name: {unit.name}",
        f" * Function count: {len(unit.functions)}",
        " *",
        " * Suggested follow-up:",
        " * - Subdivide this window into real source files once stronger corridor/object evidence exists.",
        " * - Use the listed function addresses to pull Ghidra bodies selectively when promoting a subrange.",
        " */",
        "",
        f'#include "{header_relpath}"',
        "",
        "/*",
        " * Function inventory:",
    ]
    for name, size, address in unit.functions:
        lines.append(f" * - {name} @ 0x{address:08X} size 0x{size:X}")
    lines.extend([" */", ""])
    return "\n".join(lines)


def materialize_placeholders(
    src_root: Path,
    include_root: Path,
    auto_units: list[AutoTextUnit],
) -> tuple[int, int]:
    wrote_sources = 0
    wrote_headers = 0
    for unit in auto_units:
        source_path = src_root / normalize(unit.placeholder_relpath)
        header_path = include_root / normalize(unit.placeholder_relpath.removesuffix(".c") + ".h")
        if write_text_if_changed(source_path, render_source(unit)):
            wrote_sources += 1
        if write_text_if_changed(header_path, render_header(unit)):
            wrote_headers += 1
    return wrote_sources, wrote_headers


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Claim auto-generated main text windows with stable placeholder game split files."
    )
    parser.add_argument("--report", type=Path, default=DEFAULT_REPORT_PATH)
    parser.add_argument("--splits", type=Path, default=DEFAULT_SPLITS_PATH)
    parser.add_argument("--src-root", type=Path, default=DEFAULT_SRC_ROOT)
    parser.add_argument("--include-root", type=Path, default=DEFAULT_INCLUDE_ROOT)
    parser.add_argument("--placeholder-root", default=DEFAULT_PLACEHOLDER_ROOT)
    parser.add_argument("--write", action="store_true", help="Update splits.txt and materialize placeholders")
    args = parser.parse_args()

    auto_units = parse_report_units(args.report, normalize(args.placeholder_root))
    for unit in auto_units:
        print(
            f"{unit.placeholder_relpath}: text=0x{unit.start:08X}-0x{unit.end:08X} "
            f"funcs={len(unit.functions)}"
        )

    if not args.write:
        print(f"\nFound {len(auto_units)} auto-generated main text windows.")
        return

    inserted = update_splits(args.splits, auto_units)
    wrote_sources, wrote_headers = materialize_placeholders(args.src_root, args.include_root, auto_units)
    print(
        f"\nUpdated splits with {inserted} new placeholder owner(s); "
        f"wrote {wrote_sources} source file(s) and {wrote_headers} header(s)."
    )


if __name__ == "__main__":
    main()
