def detailed_markdown(
    packets: list[SourceObjectPacket],
    widths_by_def: dict[int, Counter[int]],
) -> str:
    lines: list[str] = []
    lines.append("# Source/object packet search")
    lines.append("")
    if not packets:
        lines.append("- No matching packets.")
        return "\n".join(lines)

    for packet in packets[:16]:
        lines.append(f"## `{packet.group.retail_source_name}`")
        lines.append(
            f"- score={packet.score}, bucket=`{packet.bucket or 'unknown'}`, source=`src/{packet.suggested_relpath}`, packet=`{packet.output_relpath}`"
        )
        if packet.group.retail_labels:
            lines.append("- retail labels: " + ", ".join(f"`{label}`" for label in packet.group.retail_labels))
        if packet.group.retail_messages:
            lines.append("- retail messages: " + ", ".join(f"`{message}`" for message in packet.group.retail_messages[:4]))
        if packet.group.xrefs:
            lines.append("- EN xrefs:")
            for xref in packet.group.xrefs[:8]:
                lines.append(
                    f"  - `{format_xref(xref)}`"
                )
        if packet.reference_hint.current_debug_paths:
            lines.append("- current debug paths: " + ", ".join(f"`{path}`" for path in packet.reference_hint.current_debug_paths))
        if packet.reference_hint.reference_configure_paths:
            lines.append(
                "- reference configure paths: "
                + ", ".join(f"`{path}`" for path in packet.reference_hint.reference_configure_paths)
            )
        if packet.reference_hint.reference_dlls:
            lines.append("- reference DLL registry hints:")
            for hint in packet.reference_hint.reference_dlls[:6]:
                lines.append(f"  - `{hint.dll_id}` `{hint.dll_name}` `{hint.srcfile}`")
        if packet.reference_hint.reference_symbol_hints:
            lines.append(
                "- reference function hints: "
                + ", ".join(f"`{name}`" for name in packet.reference_hint.reference_symbol_hints[:8])
            )

        if packet.family_matches:
            lines.append("- inferred current EN DLL families:")
            for match in packet.family_matches:
                family = match.packet
                lines.append(
                    f"  - score={match.score}, dll=`0x{family.dll_id:04X}`, name=`{family.suggested_name}`, "
                    f"placements={family.total_placements}, defs={family.object_count}, packet=`src/main/unknown/{family.output_name}`"
                )
                lines.append(
                    f"    descriptor=`0x{family.dll_entry.target_address:08X}` `{family.dll_entry.target_symbol or 'unknown'}` "
                    f"slots={family.dll_entry.function_count} mask=`{family.dll_entry.slot_mask}`"
                )
                reference_functions = function_name_by_index(family.reference_dll)
                for slot in family.dll_entry.slots[:8]:
                    if slot.address == 0:
                        continue
                    ref = reference_functions.get(slot.index)
                    ref_text = f" ref={ref.name}" if ref is not None and ref.name is not None else ""
                    stub_text = f" stub={slot.stub_kind}" if slot.stub_kind is not None else ""
                    lines.append(
                        f"    slot {slot.index:02d}: `0x{slot.address:08X}` `{slot.symbol or 'unknown'}`{stub_text}{ref_text}"
                    )

        if packet.class_matches:
            lines.append("- inferred current EN class packets:")
            for match in packet.class_matches[:6]:
                class_packet = match.packet
                lines.append(
                    f"  - score={match.score}, class=`0x{class_packet.class_id:04X}`, name=`{class_packet.suggested_name}`, "
                    f"placements={class_packet.total_placements}, defs={class_packet.object_count}, "
                    f"packet=`src/main/unknown/classes/{class_packet.output_name}`"
                )

        if packet.def_matches:
            lines.append("- inferred current EN object defs:")
            for match in packet.def_matches[:8]:
                def_packet = match.packet
                widths = widths_by_def.get(def_packet.record.def_id, Counter())
                lines.append(
                    f"  - score={match.score}, def=`0x{def_packet.record.def_id:04X}`, object=`{def_packet.record.name}`, "
                    f"dll=`0x{def_packet.record.dll_id:04X}`, class=`0x{def_packet.record.class_id & 0xFFFF:04X}`, "
                    f"placements={def_packet.record.placements}, widths={format_widths(widths)}, "
                    f"packet=`src/main/unknown/objects/{def_packet.output_name}`"
                )
        lines.append("")
    return "\n".join(lines).rstrip()


def rows_to_csv(packets: list[SourceObjectPacket]) -> str:
    fieldnames = [
        "retail_source_name",
        "score",
        "bucket",
        "suggested_relpath",
        "output_relpath",
        "xref_count",
        "retail_labels",
        "current_family_ids",
        "current_family_names",
        "current_class_ids",
        "current_def_ids",
        "reference_paths",
        "reference_dlls",
        "reference_symbol_hints",
    ]
    buffer = io.StringIO()
    writer = csv.DictWriter(buffer, fieldnames=fieldnames)
    writer.writeheader()
    for packet in packets:
        writer.writerow(
            {
                "retail_source_name": packet.group.retail_source_name,
                "score": packet.score,
                "bucket": packet.bucket or "",
                "suggested_relpath": packet.suggested_relpath,
                "output_relpath": packet.output_relpath,
                "xref_count": len(packet.group.xrefs),
                "retail_labels": ",".join(packet.group.retail_labels),
                "current_family_ids": ",".join(f"0x{match.packet.dll_id:04X}" for match in packet.family_matches),
                "current_family_names": ",".join(match.packet.suggested_name for match in packet.family_matches),
                "current_class_ids": ",".join(f"0x{match.packet.class_id:04X}" for match in packet.class_matches),
                "current_def_ids": ",".join(f"0x{match.packet.record.def_id:04X}" for match in packet.def_matches),
                "reference_paths": ",".join(packet.reference_hint.current_debug_paths + packet.reference_hint.reference_configure_paths),
                "reference_dlls": ",".join(
                    f"{hint.dll_id}:{hint.dll_name}:{hint.srcfile}" for hint in packet.reference_hint.reference_dlls
                ),
                "reference_symbol_hints": ",".join(packet.reference_hint.reference_symbol_hints),
            }
        )
    return buffer.getvalue()


