GENERATED_MARKER = "Auto-generated by tools/orig/source_object_packets.py."


@dataclass(frozen=True)
class FamilyMatch:
    packet: FamilyPacket
    score: int


@dataclass(frozen=True)
class ClassMatch:
    packet: ClassPacket
    score: int


@dataclass(frozen=True)
class DefMatch:
    packet: DefPacket
    score: int


@dataclass(frozen=True)
class SourceObjectPacket:
    group: RecoveryGroup
    reference_hint: ReferenceHint
    score: int
    bucket: str | None
    suggested_relpath: str
    output_relpath: str
    source_tokens: tuple[str, ...]
    family_matches: tuple[FamilyMatch, ...]
    class_matches: tuple[ClassMatch, ...]
    def_matches: tuple[DefMatch, ...]


def normalize_separators(value: str) -> str:
    return value.replace("\\", "/")


def unique_strings(values: list[str]) -> tuple[str, ...]:
    result: list[str] = []
    seen: set[str] = set()
    for value in values:
        if not value or value in seen:
            continue
        seen.add(value)
        result.append(value)
    return tuple(result)


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def is_managed_generated_stub(path: Path) -> bool:
    if not path.is_file():
        return False
    return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="replace")[:512]


def safe_bucket(hint: ReferenceHint) -> str | None:
    if hint.current_debug_paths:
        return normalize_separators(hint.current_debug_paths[0]).split("/", 1)[0]
    if hint.reference_configure_paths:
        return normalize_separators(hint.reference_configure_paths[0]).split("/", 1)[0]
    if hint.inferred_bucket is not None:
        return hint.inferred_bucket.bucket
    return None


def suggested_relpath(group: RecoveryGroup, hint: ReferenceHint) -> str:
    if hint.current_debug_paths:
        return normalize_separators(hint.current_debug_paths[0])
    if hint.reference_configure_paths:
        return normalize_separators(hint.reference_configure_paths[0])
    return Path(group.retail_source_name).name


def token_variants(value: str) -> tuple[str, ...]:
    normalized = normalize_token(value)
    if not normalized:
        return ()
    variants = [normalized]
    if normalized.endswith("s") and len(normalized) > 4:
        variants.append(normalized[:-1])
    if normalized.endswith("es") and len(normalized) > 5:
        variants.append(normalized[:-2])
    return unique_strings(variants)


def packet_source_tokens(group: RecoveryGroup, hint: ReferenceHint) -> tuple[str, ...]:
    values = [Path(group.retail_source_name).stem]
    values.extend(Path(path).stem for path in hint.current_debug_paths)
    values.extend(Path(path).stem for path in hint.reference_configure_paths)
    values.extend(dll.dll_name for dll in hint.reference_dlls if dll.dll_name)
    values.extend(Path(dll.srcfile).stem for dll in hint.reference_dlls if dll.srcfile)
    values.extend(obj.object_name for obj in hint.object_description_hints if obj.object_name)
    values.extend(group.retail_labels)

    tokens: list[str] = []
    for value in values:
        tokens.extend(token_variants(value))
    return unique_strings(tokens)


def name_match_score(token: str, candidate: str) -> int:
    normalized = normalize_token(candidate)
    if not token or not normalized:
        return 0
    if token == normalized:
        return 100
    if len(token) >= 5 and (token.startswith(normalized) or normalized.startswith(token)):
        return 60
    if len(token) >= 6 and (token in normalized or normalized in token):
        return 30
    return 0


def match_packet_names(tokens: tuple[str, ...], names: list[str]) -> int:
    best = 0
    for token in tokens:
        for name in names:
            best = max(best, name_match_score(token, name))
    return best


def family_match_score(tokens: tuple[str, ...], packet: FamilyPacket) -> int:
    primary_names = [packet.suggested_name]
    if packet.reference_dll is not None and packet.reference_dll.name:
        primary_names.append(packet.reference_dll.name)
    secondary_names = [item.name for item in packet.objects]
    secondary_names.extend(packet.reference_class_names)

    primary = match_packet_names(tokens, primary_names)
    secondary = match_packet_names(tokens, secondary_names)
    if primary == 0 and secondary == 0:
        return 0
    return primary * 4 + secondary * 2 + min(packet.total_placements, 5000) // 50


def class_match_score(tokens: tuple[str, ...], packet: ClassPacket) -> int:
    names = [packet.suggested_name]
    if packet.class_name:
        names.append(packet.class_name)
    names.extend(item.name for item in packet.objects)
    primary = match_packet_names(tokens, names[:2])
    secondary = match_packet_names(tokens, names[2:])
    if primary == 0 and secondary == 0:
        return 0
    return primary * 4 + secondary * 2 + min(packet.total_placements, 5000) // 60


def def_match_score(tokens: tuple[str, ...], packet: DefPacket) -> int:
    names = [packet.record.name]
    if packet.class_name:
        names.append(packet.class_name)
    if packet.reference_dll is not None and packet.reference_dll.name:
        names.append(packet.reference_dll.name)
    primary = match_packet_names(tokens, names)
    if primary == 0:
        return 0
    return primary * 4 + min(packet.record.placements, 5000) // 75


def select_family_matches(tokens: tuple[str, ...], packets: list[FamilyPacket], limit: int = 3) -> tuple[FamilyMatch, ...]:
    scored = [FamilyMatch(packet=packet, score=family_match_score(tokens, packet)) for packet in packets]
    scored = [item for item in scored if item.score > 0]
    scored.sort(key=lambda item: (-item.score, -item.packet.total_placements, item.packet.dll_id))
    if not scored:
        return ()
    best = scored[0].score
    threshold = max(320, best - 90)
    return tuple(item for item in scored[:limit] if item.score >= threshold)


def select_class_matches(
    tokens: tuple[str, ...],
    packets: list[ClassPacket],
    preferred_ids: set[int],
    limit: int = 4,
) -> tuple[ClassMatch, ...]:
    scored: list[ClassMatch] = []
    for packet in packets:
        score = class_match_score(tokens, packet)
        if packet.class_id in preferred_ids:
            score += 80
        if score > 0:
            scored.append(ClassMatch(packet=packet, score=score))
    scored.sort(key=lambda item: (-item.score, -item.packet.total_placements, item.packet.class_id))
    if not scored:
        return ()
    best = scored[0].score
    threshold = max(280, best - 80)
    return tuple(item for item in scored[:limit] if item.score >= threshold)


def select_def_matches(
    tokens: tuple[str, ...],
    packets: list[DefPacket],
    preferred_ids: set[int],
    limit: int = 8,
) -> tuple[DefMatch, ...]:
    scored: list[DefMatch] = []
    for packet in packets:
        score = def_match_score(tokens, packet)
        if packet.record.def_id in preferred_ids:
            score += 80
        if score > 0:
            scored.append(DefMatch(packet=packet, score=score))
    scored.sort(key=lambda item: (-item.score, -item.packet.record.placements, item.packet.record.def_id))
    if not scored:
        return ()
    best = scored[0].score
    threshold = max(280, best - 80)
    return tuple(item for item in scored[:limit] if item.score >= threshold)


def packet_score(group: RecoveryGroup, hint: ReferenceHint, families: tuple[FamilyMatch, ...], defs: tuple[DefMatch, ...]) -> int:
    score = len(group.xrefs) * 500
    score += len(group.retail_addresses) * 40
    score += len(group.retail_labels) * 80
    score += len(hint.reference_symbol_hints) * 20
    if hint.current_debug_paths:
        score += 220
    if hint.reference_configure_paths:
        score += 120
    if hint.reference_dlls:
        score += 180
    if families:
        score += max(item.score for item in families)
        score += min(sum(item.packet.total_placements for item in families), 7000) // 12
    if defs:
        score += min(sum(item.packet.record.placements for item in defs), 6000) // 20
    return score


def build_packets(
    groups: list[RecoveryGroup],
    hints: list[ReferenceHint],
    family_packets: list[FamilyPacket],
    class_packets: list[ClassPacket],
    def_packets: list[DefPacket],
    output_root: Path,
) -> list[SourceObjectPacket]:
    hint_by_name = {hint.retail_source_name.lower(): hint for hint in hints}
    class_packets_by_id = {packet.class_id: packet for packet in class_packets}
    def_packets_by_id = {packet.record.def_id: packet for packet in def_packets}

    packets: list[SourceObjectPacket] = []
    for group in groups:
        hint = hint_by_name[group.retail_source_name.lower()]
        tokens = packet_source_tokens(group, hint)
        families = select_family_matches(tokens, family_packets)

        preferred_class_ids = {
            record.class_id & 0xFFFF
            for family in families
            for record in family.packet.objects
        }
        preferred_def_ids = {
            record.def_id
            for family in families
            for record in family.packet.objects
        }

        classes = select_class_matches(tokens, class_packets, preferred_class_ids)
        if not classes and preferred_class_ids:
            classes = tuple(
                ClassMatch(packet=class_packets_by_id[class_id], score=200)
                for class_id in sorted(preferred_class_ids)
                if class_id in class_packets_by_id
            )

        defs = select_def_matches(tokens, def_packets, preferred_def_ids)
        if not defs and preferred_def_ids:
            defs = tuple(
                DefMatch(packet=def_packets_by_id[def_id], score=200)
                for def_id in sorted(preferred_def_ids)
                if def_id in def_packets_by_id
            )

        suggested = suggested_relpath(group, hint)
        packets.append(
            SourceObjectPacket(
                group=group,
                reference_hint=hint,
                score=packet_score(group, hint, families, defs),
                bucket=safe_bucket(hint),
                suggested_relpath=suggested,
                output_relpath=normalize_separators((output_root / suggested).as_posix()),
                source_tokens=tokens,
                family_matches=families,
                class_matches=classes,
                def_matches=defs,
            )
        )

    packets.sort(
        key=lambda item: (
            -item.score,
            not item.family_matches,
            not item.group.xrefs,
            item.suggested_relpath.lower(),
        )
    )
    return packets


def packet_matches(packet: SourceObjectPacket, pattern: str) -> bool:
    lowered = pattern.lower()
    if pattern.startswith("dll:"):
        value = lowered[4:]
        return any(value in (f"{match.packet.dll_id:04x}", f"0x{match.packet.dll_id:04x}") for match in packet.family_matches)
    if pattern.startswith("class:"):
        value = lowered[6:]
        return any(value in (f"{match.packet.class_id:04x}", f"0x{match.packet.class_id:04x}") for match in packet.class_matches)
    if pattern.startswith("def:"):
        value = lowered[4:]
        return any(value in (f"{match.packet.record.def_id:04x}", f"0x{match.packet.record.def_id:04x}") for match in packet.def_matches)

    fields = [
        packet.group.retail_source_name.lower(),
        packet.suggested_relpath.lower(),
        *(token.lower() for token in packet.source_tokens),
        *(label.lower() for label in packet.group.retail_labels),
        *(message.lower() for message in packet.group.retail_messages),
        *(xref.function_name.lower() for xref in packet.group.xrefs if xref.function_name),
        *(hint.lower() for hint in packet.reference_hint.reference_symbol_hints),
        *(path.lower() for path in packet.reference_hint.current_debug_paths),
        *(path.lower() for path in packet.reference_hint.reference_configure_paths),
        *(match.packet.suggested_name.lower() for match in packet.family_matches),
        *(match.packet.record.name.lower() for match in packet.def_matches),
    ]
    return any(lowered in value for value in fields)


def filter_packets(packets: list[SourceObjectPacket], patterns: list[str]) -> list[SourceObjectPacket]:
    if not patterns:
        return packets
    return [packet for packet in packets if all(packet_matches(packet, pattern) for pattern in patterns)]


def format_widths(counter: Counter[int]) -> str:
    return ", ".join(f"{width}w x{count}" for width, count in sorted(counter.items())) or "none"


def format_xref(xref) -> str:
    function_name = xref.function_name or "unknown"
    if xref.function_start is None:
        return f"0x{xref.xref_address:08X} {function_name}"
    return f"0x{xref.xref_address:08X} {function_name} + 0x{xref.xref_address - xref.function_start:X}"


