def main(argv: list[str] | None = None) -> int:
    parser = build_arg_parser()
    args = parser.parse_args(argv)

    groups = build_groups(
        dol=args.dol,
        symbols=args.symbols,
        debug_symbols=args.debug_symbols,
        debug_splits=args.debug_splits,
        debug_srcfiles=args.debug_srcfiles,
    )
    reference_hints = collect_reference_hints(
        groups=groups,
        reference_configure=args.reference_configure,
        reference_symbols=args.reference_symbols,
        reference_inventory=args.reference_inventory,
        reference_dll_registry=args.reference_dll_registry,
        reference_object_xmls=tuple(args.reference_object_xml),
    )

    reference_class_path = resolve_reference_path(args.reference_classes, DEFAULT_REFERENCE_CLASS_CANDIDATES)
    reference_object_path = resolve_reference_path(args.reference_objects, DEFAULT_REFERENCE_OBJECT_CANDIDATES)
    reference_dll_path = resolve_reference_path(args.reference_dlls, DEFAULT_REFERENCE_DLL_CANDIDATES)

    reference_class_names = load_reference_class_names(reference_class_path)
    reference_objects_by_def, reference_objects_by_name = load_reference_object_hints(reference_object_path)
    reference_dll_hints = load_reference_dll_hints(reference_dll_path or Path("__missing__"))

    objindex = load_objindex(args.files_root / "OBJINDEX.bin")
    placements, romlists_by_def = load_romlist_usage(args.files_root, objindex)
    records, _raw_offsets = build_records(args.files_root, placements, romlists_by_def)
    widths_by_def, _romlists_by_def_width = load_root_param_widths(args.files_root, objindex)

    symbols = load_symbols(args.symbols)
    object_defs, objindex = load_object_defs(args.files_root)
    objects_by_dll: dict[int, list] = defaultdict(list)
    for object_def in object_defs:
        objects_by_dll[object_def.dll_id].append(object_def)

    placements_by_dll = load_dll_placement_counts(args.files_root, object_defs, objindex)
    dol = DolFile(args.dol)
    table_address = args.dll_table if args.dll_table is not None else guess_dll_table_address(symbols, MAX_DLL_ID)
    dll_entries = parse_dll_entries(
        dol=dol,
        table_address=table_address,
        max_dll_id=MAX_DLL_ID,
        symbols=symbols,
        objects_by_dll=objects_by_dll,
        placements_by_dll=placements_by_dll,
    )

    family_packets = build_family_packets(records, dll_entries, widths_by_def, reference_dll_hints, reference_class_names)
    class_packets = build_class_packets(
        records,
        widths_by_def,
        dll_entries,
        reference_class_names,
        reference_objects_by_def,
        reference_objects_by_name,
    )
    def_packets = build_def_packets(
        records,
        widths_by_def,
        dll_entries,
        reference_class_names,
        reference_objects_by_def,
        reference_objects_by_name,
        reference_dll_hints,
    )

    packets = build_packets(groups, reference_hints, family_packets, class_packets, def_packets, args.output_root)
    visible = filter_packets(packets, args.search)

    if args.materialize_all:
        materialized = visible
    elif args.materialize_top > 0:
        materialized = visible[: args.materialize_top]
    else:
        materialized = []

    if materialized:
        written, skipped = materialize_packets(materialized, args.output_root, widths_by_def)
        print(f"materialized={written} skipped_existing={skipped}", file=sys.stderr)

    try:
        if args.format == "csv":
            sys.stdout.write(rows_to_csv(visible))
        elif args.search:
            sys.stdout.write(detailed_markdown(visible, widths_by_def))
            sys.stdout.write("\n")
        else:
            sys.stdout.write(summary_markdown(visible, args.limit))
            sys.stdout.write("\n")
    except BrokenPipeError:
        pass
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
