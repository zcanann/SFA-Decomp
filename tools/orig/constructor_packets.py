GENERATED_MARKER = "Auto-generated by tools/orig/constructor_packets.py."
IDENTIFIER_RE = re.compile(r"[^A-Za-z0-9_]+")


@dataclass(frozen=True)
class FunctionContext:
    address: int
    display_name: str
    current_source: DebugSourceFile | None


@dataclass(frozen=True)
class StringEvidence:
    xref: StringXref
    string_entry: DolString
    retail_source_name: str | None
    retail_label: str | None
    retail_message: str | None
    current_source: DebugSourceFile | None


@dataclass(frozen=True)
class ConstructorPacket:
    candidate: TableCandidate
    score: int
    primary_store: TableStoreXref
    output_name: str
    constructor_functions: tuple[FunctionContext, ...]
    method_functions: tuple[FunctionContext, ...]
    current_sources: tuple[DebugSourceFile, ...]
    source_evidence: tuple[StringEvidence, ...]
    string_evidence: tuple[StringEvidence, ...]


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def is_managed_generated_stub(path: Path) -> bool:
    if not path.is_file():
        return False
    return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="replace")[:512]


def slugify(value: str) -> str:
    slug = IDENTIFIER_RE.sub("_", value).strip("_").lower()
    return slug or "packet"


def output_name_for_packet(primary_store: TableStoreXref, candidate: TableCandidate) -> str:
    if candidate.kind == "vtable-like":
        kind = "vtable"
    elif candidate.kind == "callback-table-like":
        kind = "callback_table"
    else:
        kind = "pointer_table"
    function_start = primary_store.function_start or primary_store.load_address
    return f"ctor_{function_start:08X}_{kind}.c"


def dedupe_sources(values: list[DebugSourceFile]) -> tuple[DebugSourceFile, ...]:
    result: list[DebugSourceFile] = []
    seen: set[str] = set()
    for value in values:
        if value.path in seen:
            continue
        seen.add(value.path)
        result.append(value)
    return tuple(result)


def dedupe_function_contexts(values: list[FunctionContext]) -> tuple[FunctionContext, ...]:
    result: list[FunctionContext] = []
    seen: set[int] = set()
    for value in values:
        if value.address in seen:
            continue
        seen.add(value.address)
        result.append(value)
    return tuple(result)


def dedupe_string_evidence(values: list[StringEvidence]) -> tuple[StringEvidence, ...]:
    result: list[StringEvidence] = []
    seen: set[tuple[int, int]] = set()
    for value in values:
        key = (value.xref.xref_address, value.xref.target_address)
        if key in seen:
            continue
        seen.add(key)
        result.append(value)
    return tuple(result)


def current_source_for_address(sources: tuple[DebugSourceFile, ...], address: int) -> DebugSourceFile | None:
    for source in sources:
        if source.text_start <= address < source.text_end:
            return source
    return None


def build_function_contexts(
    addresses: list[int],
    symbols,
    current_sources: tuple[DebugSourceFile, ...],
) -> tuple[FunctionContext, ...]:
    contexts: list[FunctionContext] = []
    for address in addresses:
        function = function_for_address(symbols, address)
        display_name = format_symbol_name(function, address)
        contexts.append(
            FunctionContext(
                address=address,
                display_name=display_name,
                current_source=current_source_for_address(current_sources, address),
            )
        )
    return dedupe_function_contexts(contexts)


def build_string_evidence(
    xrefs: list[StringXref],
    strings_by_address: dict[int, DolString],
    current_sources: tuple[DebugSourceFile, ...],
) -> tuple[StringEvidence, ...]:
    evidence: list[StringEvidence] = []
    for xref in xrefs:
        string_entry = strings_by_address[xref.target_address]
        retail_source_name = extract_source_name(string_entry.text) if "source" in string_entry.tags else None
        retail_label = None
        retail_message = None
        if retail_source_name is not None:
            retail_label, retail_message = extract_retail_context(string_entry.text, retail_source_name)
        evidence.append(
            StringEvidence(
                xref=xref,
                string_entry=string_entry,
                retail_source_name=retail_source_name,
                retail_label=retail_label,
                retail_message=retail_message,
                current_source=current_source_for_address(current_sources, xref.xref_address),
            )
        )
    return dedupe_string_evidence(evidence)


def packet_score(candidate: TableCandidate, source_hits: int, string_hits: int, source_paths: int) -> int:
    score = 1000 if candidate.kind == "vtable-like" else 700
    score += min(candidate.slot_count, 16) * 20
    score += sum(120 if xref.store_disp == 0 else 40 for xref in candidate.store_xrefs)
    score += source_hits * 80
    score += string_hits * 15
    score += source_paths * 10
    return score


def build_packets(
    candidates: list[TableCandidate],
    symbols,
    current_sources: tuple[DebugSourceFile, ...],
    strings_by_address: dict[int, DolString],
    xrefs: list[StringXref],
) -> list[ConstructorPacket]:
    xrefs_by_function: dict[int, list[StringXref]] = {}
    for xref in xrefs:
        if xref.function_start is None:
            continue
        xrefs_by_function.setdefault(xref.function_start, []).append(xref)

    packets: list[ConstructorPacket] = []
    for candidate in candidates:
        if not candidate.store_xrefs:
            continue
        store_xrefs = sorted(
            candidate.store_xrefs,
            key=lambda item: (
                item.store_disp != 0,
                item.function_start is None,
                item.function_start or item.load_address,
                item.store_address,
            ),
        )
        primary_store = store_xrefs[0]
        constructor_addrs = [item.function_start or item.load_address for item in store_xrefs]
        method_addrs = list(candidate.method_addresses)
        constructor_functions = build_function_contexts(constructor_addrs, symbols, current_sources)
        method_functions = build_function_contexts(method_addrs, symbols, current_sources)

        related_function_starts = {
            item.function_start
            for item in store_xrefs
            if item.function_start is not None
        }
        for method_address in candidate.method_addresses:
            function = function_for_address(symbols, method_address)
            if function is not None:
                related_function_starts.add(function.address)

        relevant_xrefs = [
            xref
            for function_start in sorted(related_function_starts)
            for xref in xrefs_by_function.get(function_start, [])
        ]
        evidence = build_string_evidence(relevant_xrefs, strings_by_address, current_sources)
        source_evidence = tuple(item for item in evidence if "source" in item.string_entry.tags)
        current_source_list = dedupe_sources(
            [context.current_source for context in (*constructor_functions, *method_functions) if context.current_source is not None]
            + [item.current_source for item in evidence if item.current_source is not None]
        )
        score = packet_score(candidate, len(source_evidence), len(evidence), len(current_source_list))
        packets.append(
            ConstructorPacket(
                candidate=candidate,
                score=score,
                primary_store=primary_store,
                output_name=output_name_for_packet(primary_store, candidate),
                constructor_functions=constructor_functions,
                method_functions=method_functions,
                current_sources=current_source_list,
                source_evidence=source_evidence,
                string_evidence=evidence,
            )
        )

    packets.sort(
        key=lambda item: (
            -item.score,
            item.primary_store.function_start or item.primary_store.load_address,
            item.candidate.start_address,
        )
    )
    return packets


def search_packets(packets: list[ConstructorPacket], patterns: list[str]) -> list[ConstructorPacket]:
    lowered = [pattern.lower() for pattern in patterns]
    matches: list[ConstructorPacket] = []
    for packet in packets:
        fields = [
            packet.output_name.lower(),
            packet.candidate.kind.lower(),
            f"{packet.candidate.start_address:08x}",
            f"{packet.primary_store.function_start or packet.primary_store.load_address:08x}",
        ]
        fields.extend(context.display_name.lower() for context in packet.constructor_functions)
        fields.extend(context.display_name.lower() for context in packet.method_functions)
        fields.extend(source.path.lower() for source in packet.current_sources)
        fields.extend(item.string_entry.text.lower() for item in packet.string_evidence)
        fields.extend((item.retail_source_name or "").lower() for item in packet.source_evidence)
        if any(any(pattern in field for field in fields) for pattern in lowered):
            matches.append(packet)
    return matches


def summary_markdown(packets: list[ConstructorPacket]) -> str:
    primary_packets = [packet for packet in packets if packet.candidate.kind == "vtable-like"]
    callback_packets = [packet for packet in packets if packet.candidate.kind == "callback-table-like"]

    lines: list[str] = []
    lines.append("# Retail constructor-backed packet audit")
    lines.append("")
    lines.append("## Summary")
    lines.append(f"- Store-backed packets recovered: `{len(packets)}`")
    lines.append(f"- Primary vtable-like packets: `{len(primary_packets)}`")
    lines.append(f"- Callback-table-like packets: `{len(callback_packets)}`")
    lines.append(
        f"- Packets with direct retail source-tag evidence: `{sum(1 for packet in packets if packet.source_evidence)}`"
    )
    lines.append(
        f"- Packets with current EN split coverage: `{sum(1 for packet in packets if packet.current_sources)}`"
    )
    lines.append("")
    lines.append("## Packets")
    for packet in packets:
        constructor_name = packet.constructor_functions[0].display_name if packet.constructor_functions else "unknown"
        store = packet.primary_store
        source_text = ", ".join(f"`{item.retail_source_name}`" for item in packet.source_evidence[:3]) or "none"
        current_text = ", ".join(f"`{source.path}`" for source in packet.current_sources[:3]) or "none"
        lines.append(
            f"- `{constructor_name}`: score={packet.score}, kind=`{packet.candidate.kind}`, "
            f"table=`0x{packet.candidate.start_address:08X}`, store=`r{store.object_reg}+0x{store.store_disp:X}`, "
            f"sources={source_text}, current={current_text}, packet=`src/main/unknown/constructors/{packet.output_name}`"
        )
    return "\n".join(lines)


def packet_markdown(packet: ConstructorPacket) -> str:
    lines: list[str] = []
    lines.append(
        f"# Constructor packet for `{packet.constructor_functions[0].display_name if packet.constructor_functions else 'unknown'}`"
    )
    lines.append("")
    lines.append(
        f"- score={packet.score}, kind=`{packet.candidate.kind}`, table=`0x{packet.candidate.start_address:08X}`, "
        f"loaded=`0x{packet.primary_store.loaded_table_address:08X}`, slots=`{packet.candidate.slot_count}`"
    )
    lines.append(
        f"- primary store=`{packet.primary_store.function_name or 'unknown'}+0x{packet.primary_store.store_address - (packet.primary_store.function_start or packet.primary_store.store_address):X}` "
        f"-> `r{packet.primary_store.object_reg}+0x{packet.primary_store.store_disp:X}`"
    )
    if packet.current_sources:
        lines.append("- current EN split coverage:")
        for source in packet.current_sources:
            lines.append(
                f"  - `{source.path}` `.text 0x{source.text_start:08X}-0x{source.text_end:08X}`"
            )
    if packet.constructor_functions:
        lines.append("- constructor-like functions:")
        for context in packet.constructor_functions:
            source_text = (
                f", current=`{context.current_source.path}`"
                if context.current_source is not None
                else ""
            )
            lines.append(f"  - `0x{context.address:08X}` `{context.display_name}`{source_text}")
    lines.append("- method slots:")
    for index, context in enumerate(packet.method_functions):
        source_text = (
            f", current=`{context.current_source.path}`"
            if context.current_source is not None
            else ""
        )
        lines.append(f"  - slot {index:02d}: `0x{context.address:08X}` `{context.display_name}`{source_text}")
    if packet.source_evidence:
        lines.append("- retail source-tag evidence:")
        for item in packet.source_evidence:
            context_parts: list[str] = []
            if item.retail_label:
                context_parts.append(f"label=`{item.retail_label}`")
            if item.retail_message:
                context_parts.append(f"message=`{item.retail_message}`")
            if item.current_source is not None:
                context_parts.append(f"current=`{item.current_source.path}`")
            extra = ", ".join(context_parts)
            suffix = f" ({extra})" if extra else ""
            function_text = item.xref.function_name or "unknown"
            lines.append(
                f"  - `0x{item.xref.target_address:08X}` `{item.retail_source_name}` xref=`{function_text}` @ `0x{item.xref.xref_address:08X}`{suffix}"
            )
    if packet.string_evidence:
        lines.append("- related retail strings:")
        for item in packet.string_evidence[:10]:
            current_text = (
                f", current=`{item.current_source.path}`"
                if item.current_source is not None
                else ""
            )
            lines.append(
                f"  - `0x{item.xref.target_address:08X}` `{item.string_entry.text}` xref=`{item.xref.function_name or 'unknown'}` @ `0x{item.xref.xref_address:08X}`{current_text}"
            )
    return "\n".join(lines)


def render_stub(packet: ConstructorPacket) -> str:
    constructor_name = packet.constructor_functions[0].display_name if packet.constructor_functions else "unknown"
    lines: list[str] = []
    lines.append("/*")
    lines.append(f" * {GENERATED_MARKER}")
    lines.append(" *")
    lines.append(" * This file is intentionally not wired into the build yet.")
    lines.append(" * It exists as a retail-backed constructor/class packet for exploratory split work.")
    lines.append(" *")
    lines.append(f" * Constructor: {constructor_name}")
    lines.append(f" * Packet path: src/main/unknown/constructors/{packet.output_name}")
    lines.append(f" * Score: {packet.score}")
    lines.append(f" * Kind: {packet.candidate.kind}")
    lines.append(
        f" * Retail table: 0x{packet.candidate.start_address:08X} (loaded as 0x{packet.primary_store.loaded_table_address:08X})"
    )
    lines.append(
        f" * Primary store: {packet.primary_store.function_name or 'unknown'} stores to r{packet.primary_store.object_reg}+0x{packet.primary_store.store_disp:X}"
    )
    if packet.current_sources:
        lines.append(" *")
        lines.append(" * Current EN split coverage:")
        for source in packet.current_sources:
            lines.append(f" * - {source.path} (.text 0x{source.text_start:08X}-0x{source.text_end:08X})")
    lines.append(" *")
    lines.append(" * Constructor-like functions:")
    for context in packet.constructor_functions:
        source_text = f" current={context.current_source.path}" if context.current_source is not None else ""
        lines.append(f" * - 0x{context.address:08X} {context.display_name}{source_text}")
    lines.append(" *")
    lines.append(" * Method slots:")
    for index, context in enumerate(packet.method_functions):
        source_text = f" current={context.current_source.path}" if context.current_source is not None else ""
        lines.append(f" * - slot {index:02d}: 0x{context.address:08X} {context.display_name}{source_text}")
    if packet.source_evidence:
        lines.append(" *")
        lines.append(" * Retail source-tag evidence:")
        for item in packet.source_evidence:
            label_text = f" label={item.retail_label}" if item.retail_label else ""
            message_text = f" message={item.retail_message}" if item.retail_message else ""
            lines.append(
                f" * - 0x{item.xref.target_address:08X} {item.retail_source_name} via {item.xref.function_name or 'unknown'} @ 0x{item.xref.xref_address:08X}{label_text}{message_text}"
            )
    elif packet.string_evidence:
        lines.append(" *")
        lines.append(" * Related retail strings:")
        for item in packet.string_evidence[:8]:
            lines.append(
                f" * - 0x{item.xref.target_address:08X} {item.string_entry.text} via {item.xref.function_name or 'unknown'} @ 0x{item.xref.xref_address:08X}"
            )
    lines.append(" */")
    lines.append("")
    lines.append("#if 0")
    lines.append("/*")
    lines.append(" * Packet constants only. Promote names and boundaries after EN inspection.")
    lines.append(" */")
    lines.append("enum constructor_packet_info {")
    lines.append(f"    PACKET_TABLE_ADDR = 0x{packet.candidate.start_address:08X},")
    lines.append(f"    PACKET_TABLE_LOADED_ADDR = 0x{packet.primary_store.loaded_table_address:08X},")
    lines.append(
        f"    PACKET_CONSTRUCTOR_ADDR = 0x{packet.primary_store.function_start or packet.primary_store.load_address:08X},"
    )
    lines.append(f"    PACKET_SLOT_COUNT = {packet.candidate.slot_count},")
    lines.append(f"    PACKET_STORE_DISP = 0x{packet.primary_store.store_disp & 0xFFFFFFFF:08X},")
    lines.append("};")
    lines.append("#endif")
    lines.append("")
    return "\n".join(lines)


def summary_csv(packets: list[ConstructorPacket]) -> str:
    output = io.StringIO()
    writer = csv.writer(output)
    writer.writerow(
        [
            "score",
            "kind",
            "table_address",
            "loaded_address",
            "constructor_address",
            "constructor_name",
            "store_disp",
            "object_reg",
            "slot_count",
            "current_sources",
            "retail_sources",
            "packet_path",
        ]
    )
    for packet in packets:
        constructor_name = packet.constructor_functions[0].display_name if packet.constructor_functions else "unknown"
        writer.writerow(
            [
                packet.score,
                packet.candidate.kind,
                f"0x{packet.candidate.start_address:08X}",
                f"0x{packet.primary_store.loaded_table_address:08X}",
                f"0x{packet.primary_store.function_start or packet.primary_store.load_address:08X}",
                constructor_name,
                f"0x{packet.primary_store.store_disp & 0xFFFFFFFF:08X}",
                packet.primary_store.object_reg,
                packet.candidate.slot_count,
                ";".join(source.path for source in packet.current_sources),
                ";".join(item.retail_source_name or "" for item in packet.source_evidence),
                f"src/main/unknown/constructors/{packet.output_name}",
            ]
        )
    return output.getvalue()


def materialize_packets(
    packets: list[ConstructorPacket],
    output_root: Path,
    limit: int | None = None,
) -> list[Path]:
    written: list[Path] = []
    selected = packets if limit is None else packets[:limit]
    for packet in selected:
        path = output_root / packet.output_name
        text = render_stub(packet)
        changed = write_text_if_changed(path, text)
        if changed or path.is_file():
            written.append(path)
    existing_paths = {output_root / packet.output_name for packet in selected}
    if output_root.is_dir():
        for path in output_root.glob("*.c"):
            if path in existing_paths or not is_managed_generated_stub(path):
                continue
            path.unlink()
    return written


def build_argument_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Turn store-backed retail DOL vtable/callback tables into constructor packets."
    )
    parser.add_argument("--dol", type=Path, default=Path("orig/GSAE01/sys/main.dol"), help="Retail EN main.dol path.")
    parser.add_argument("--symbols", type=Path, default=Path("config/GSAE01/symbols.txt"), help="Current EN symbols path.")
    parser.add_argument("--splits", type=Path, default=Path("config/GSAE01/splits.txt"), help="Current EN splits path.")
    parser.add_argument("--min-methods", type=int, default=4, help="Minimum pointer-table length to consider.")
    parser.add_argument("--max-methods", type=int, default=12, help="Maximum pointer-table length to consider.")
    parser.add_argument(
        "--stores-only",
        action="store_true",
        help="Compatibility flag. Constructor packets are always store-backed.",
    )
    parser.add_argument("--search", nargs="+", help="Case-insensitive substrings to filter packets.")
    parser.add_argument("--format", choices=("markdown", "csv"), default="markdown", help="Output format.")
    parser.add_argument("--materialize-top", type=int, default=0, help="Materialize the top N packet stubs under src/main/unknown/constructors.")
    parser.add_argument("--materialize-all", action="store_true", help="Materialize every shown packet stub.")
    parser.add_argument("--output-root", type=Path, default=Path("src/main/unknown/constructors"), help="Destination directory for packet stubs when materializing.")
    return parser


def main() -> None:
    parser = build_argument_parser()
    args = parser.parse_args()

    current_functions = load_function_symbols(args.symbols)
    split_ranges = parse_debug_split_text_ranges(args.splits)
    current_sources_by_name, _current_sources_by_path = build_debug_sources(
        split_ranges,
        current_functions,
        set(),
    )
    current_sources = tuple(
        sorted(
            [source for values in current_sources_by_name.values() for source in values],
            key=lambda item: (item.text_start, item.path.lower()),
        )
    )

    vtable_dol = VtableDolFile(args.dol)
    candidates = find_pointer_tables(vtable_dol, args.min_methods, args.max_methods)
    candidates = attach_store_xrefs(vtable_dol, candidates, current_functions)
    candidates = [candidate for candidate in candidates if candidate.store_xrefs]

    xref_dol = XrefDolFile(args.dol)
    strings = scan_strings(xref_dol)
    strings_by_address = {entry.address: entry for entry in strings}
    xrefs = scan_text_xrefs(xref_dol, strings_by_address, current_functions)
    _xrefs_by_target = group_xrefs_by_target(xrefs)

    packets = build_packets(candidates, current_functions, current_sources, strings_by_address, xrefs)
    if args.search:
        packets = search_packets(packets, args.search)

    if args.format == "csv":
        sys.stdout.write(summary_csv(packets))
    else:
        sys.stdout.write(summary_markdown(packets))
        if args.search and packets:
            sys.stdout.write("\n\n## Search detail\n")
            for packet in packets:
                sys.stdout.write("\n")
                sys.stdout.write(packet_markdown(packet))
                sys.stdout.write("\n")

    if args.materialize_all:
        written = materialize_packets(packets, args.output_root, limit=None)
    elif args.materialize_top > 0:
        written = materialize_packets(packets, args.output_root, limit=args.materialize_top)
    else:
        written = []

    if written and args.format == "markdown":
        sys.stdout.write("\n\n## Materialized\n")
        for path in written:
            sys.stdout.write(f"\n- `{path.as_posix()}`")


if __name__ == "__main__":
    main()
