def detailed_markdown(
    packets: list[ClassPacket],
    widths_by_def: dict[int, Counter[int]],
    romlists_by_def_width: dict[tuple[int, int], set[str]],
    dll_entries_by_id: dict[int, DllEntry],
    reference_objects_by_def: dict[int, ReferenceObjectHint],
    reference_objects_by_name: dict[str, ReferenceObjectHint],
) -> str:
    lines: list[str] = []
    lines.append("# Object-class search")
    lines.append("")
    if not packets:
        lines.append("- No matching class packets.")
        return "\n".join(lines)

    for packet in packets[:16]:
        lines.append(f"## `0x{packet.class_id:04X}` `{packet.suggested_name}`")
        lines.append(
            f"- score={packet.score}, placements={packet.total_placements}, defs={packet.object_count}, "
            f"dlls={len(packet.dll_ids)} [{format_dlls(packet.dll_ids)}], record_sizes={format_sizes_hex(packet.record_sizes)}, "
            f"placement_widths={format_widths(packet.placement_widths)}"
        )
        lines.append(f"- suggested packet path: `src/main/unknown/classes/{packet.output_name}`")
        if packet.class_name:
            lines.append(f"- reference class name: `{packet.class_name}`")
        if packet.map_names:
            lines.append("- fixed map hints: " + ", ".join(f"`{name}`" for name in packet.map_names[:10]))

        lines.append("- retail objects:")
        for item in packet.objects[:12]:
            width_counter = widths_by_def.get(item.def_id, Counter())
            width_text = ", ".join(f"{width}w x{count}" for width, count in sorted(width_counter.items())) or "none"
            alias_text = "" if not item.remap_sources else f", aliases={' '.join(f'0x{value:04X}' for value in item.remap_sources)}"
            map_text = "" if item.map_name is None else f", map={item.map_name}"
            lines.append(
                f"  - `0x{item.def_id:04X}` `{item.name}`: dll=`0x{item.dll_id:04X}`, "
                f"placements={item.placements}, romlists={item.romlists}, objsize=`0x{item.size:X}`, widths={width_text}{map_text}{alias_text}"
            )
            hint = reference_hint_for_record(item, reference_objects_by_def, reference_objects_by_name)
            if hint is not None and hint.params:
                params = ", ".join(
                    f"0x{param.offset:02X}:{param.name or 'unnamed'}:{param.type_name}"
                    for param in hint.params[:6]
                )
                lines.append(f"    ref params: {params}")
            if width_counter:
                width_samples = []
                for width in sorted(width_counter):
                    sample_names = sorted(romlists_by_def_width.get((item.def_id, width), set()))[:3]
                    if sample_names:
                        width_samples.append(f"{width}w via {' '.join(sample_names)}")
                if width_samples:
                    lines.append("    samples: " + "; ".join(width_samples))
        if len(packet.objects) > 12:
            lines.append(f"  - ... {len(packet.objects) - 12} more objects omitted")

        descriptor_entries = [
            dll_entries_by_id[dll_id]
            for dll_id in packet.descriptor_dll_ids
            if dll_id in dll_entries_by_id
        ]
        if descriptor_entries:
            lines.append("- descriptor DLLs:")
            for entry in descriptor_entries[:6]:
                lines.append(
                    f"  - `0x{entry.dll_id:04X}` `{entry.target_symbol or 'unknown'}`: "
                    f"slots={entry.function_count}, mask=`{entry.slot_mask}`"
                )
                for slot in entry.slots[:6]:
                    if slot.address == 0:
                        continue
                    lines.append(
                        f"    slot {slot.index:02d}: `0x{slot.address:08X}` `{slot.symbol or 'unknown'}`"
                    )
        lines.append("")
    return "\n".join(lines).rstrip()


def rows_to_csv(packets: list[ClassPacket]) -> str:
    fieldnames = [
        "class_id",
        "class_id_hex",
        "score",
        "class_name",
        "suggested_name",
        "placements",
        "object_defs",
        "dll_ids",
        "descriptor_dll_ids",
        "record_sizes",
        "placement_widths",
        "maps",
        "reference_param_count",
        "output_name",
        "sample_objects",
    ]
    buffer = io.StringIO()
    writer = csv.DictWriter(buffer, fieldnames=fieldnames)
    writer.writeheader()
    for packet in packets:
        writer.writerow(
            {
                "class_id": packet.class_id,
                "class_id_hex": f"0x{packet.class_id:04X}",
                "score": packet.score,
                "class_name": packet.class_name,
                "suggested_name": packet.suggested_name,
                "placements": packet.total_placements,
                "object_defs": packet.object_count,
                "dll_ids": " ".join(f"0x{dll_id:04X}" for dll_id in packet.dll_ids),
                "descriptor_dll_ids": " ".join(f"0x{dll_id:04X}" for dll_id in packet.descriptor_dll_ids),
                "record_sizes": " ".join(f"0x{size:X}" for size in packet.record_sizes),
                "placement_widths": " ".join(f"{width}w" for width in packet.placement_widths),
                "maps": " | ".join(packet.map_names),
                "reference_param_count": packet.reference_param_count,
                "output_name": packet.output_name,
                "sample_objects": " | ".join(f"0x{item.def_id:04X}:{item.name}" for item in packet.objects[:6]),
            }
        )
    return buffer.getvalue()


