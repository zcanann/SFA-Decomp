@dataclass(frozen=True)
class ReferenceParamHint:
    offset: int
    type_name: str
    name: str | None
    description: str | None


@dataclass(frozen=True)
class ReferenceObjectHint:
    def_id: int | None
    object_id: int | None
    name: str | None
    class_id: int | None
    dll_id: int | None
    description: str | None
    params: tuple[ReferenceParamHint, ...]


@dataclass(frozen=True)
class ClassPacket:
    class_id: int
    score: int
    class_name: str
    suggested_name: str
    output_name: str
    total_placements: int
    object_count: int
    dll_ids: tuple[int, ...]
    descriptor_dll_ids: tuple[int, ...]
    record_sizes: tuple[int, ...]
    placement_widths: tuple[int, ...]
    map_names: tuple[str, ...]
    objects: tuple[ObjectRecord, ...]
    reference_param_count: int
    referenced_object_count: int


def clean_text(value: str | None) -> str | None:
    if value is None:
        return None
    text = " ".join(value.split())
    return text or None


def parse_int(value: str | None) -> int | None:
    if value is None or value == "":
        return None
    return int(value, 0)


def slugify(value: str) -> str:
    slug = IDENTIFIER_RE.sub("_", value).strip("_").lower()
    return slug or "class"


def identifier_from_name(value: str) -> str:
    identifier = IDENTIFIER_RE.sub("_", value).strip("_")
    if not identifier:
        identifier = "unnamed"
    if identifier[0].isdigit():
        identifier = "_" + identifier
    return identifier


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def is_managed_generated_stub(path: Path) -> bool:
    if not path.is_file():
        return False
    return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="replace")[:512]


def resolve_reference_path(explicit: Path | None, candidates: tuple[Path, ...]) -> Path | None:
    if explicit is not None:
        return explicit if explicit.is_file() else None
    for candidate in candidates:
        if candidate.is_file():
            return candidate
    return None


def load_reference_class_names(path: Path | None) -> dict[int, str]:
    if path is None or not path.is_file():
        return {}
    root = ET.parse(path).getroot()
    names: dict[int, str] = {}
    for element in root.findall("cat"):
        class_id = parse_int(element.attrib.get("id"))
        name = clean_text(element.attrib.get("name"))
        if class_id is None or name is None:
            continue
        names[class_id] = name
    return names


def load_reference_object_hints(path: Path | None) -> tuple[dict[int, ReferenceObjectHint], dict[str, ReferenceObjectHint]]:
    if path is None or not path.is_file():
        return {}, {}

    by_def: dict[int, ReferenceObjectHint] = {}
    by_name: dict[str, ReferenceObjectHint] = {}
    root = ET.parse(path).getroot()
    for element in root.findall("object"):
        params: list[ReferenceParamHint] = []
        params_parent = element.find("params")
        if params_parent is not None:
            for param_element in params_parent.findall("param"):
                offset = parse_int(param_element.attrib.get("offset"))
                if offset is None:
                    continue
                params.append(
                    ReferenceParamHint(
                        offset=offset,
                        type_name=clean_text(param_element.attrib.get("type")) or "unknown",
                        name=clean_text(param_element.attrib.get("name")),
                        description=clean_text(
                            param_element.attrib.get("description")
                            or param_element.attrib.get("note")
                            or param_element.text
                        ),
                    )
                )

        hint = ReferenceObjectHint(
            def_id=parse_int(element.attrib.get("def")),
            object_id=parse_int(element.attrib.get("id")),
            name=clean_text(element.attrib.get("name")),
            class_id=parse_int(element.attrib.get("cat")),
            dll_id=parse_int(element.attrib.get("dll")),
            description=clean_text(element.findtext("description")),
            params=tuple(sorted(params, key=lambda item: item.offset)),
        )
        if hint.def_id is not None:
            by_def[hint.def_id] = hint
        if hint.name is not None:
            by_name[hint.name.lower()] = hint
    return by_def, by_name


def load_root_param_widths(
    files_root: Path,
    objindex: list[int],
) -> tuple[dict[int, Counter[int]], dict[tuple[int, int], set[str]]]:
    widths_by_def: dict[int, Counter[int]] = defaultdict(Counter)
    romlists_by_def_width: dict[tuple[int, int], set[str]] = defaultdict(set)
    for romlist_path in sorted(files_root.glob("*.romlist.zlb")):
        decompressed = decompress_zlb(romlist_path)
        offset = 0
        while offset < len(decompressed):
            object_id = int.from_bytes(decompressed[offset : offset + 2], "big", signed=True)
            size_words = decompressed[offset + 2]
            record_size = size_words * 4
            if record_size <= 0 or offset + record_size > len(decompressed):
                raise ValueError(
                    f"Invalid record in {romlist_path.name}: offset=0x{offset:X} size_words={size_words}"
                )
            canonical_id = resolve_object_id(objindex, object_id)
            widths_by_def[canonical_id][size_words] += 1
            romlists_by_def_width[(canonical_id, size_words)].add(romlist_path.name)
            offset += record_size
    return widths_by_def, romlists_by_def_width


def reference_hint_for_record(
    record: ObjectRecord,
    by_def: dict[int, ReferenceObjectHint],
    by_name: dict[str, ReferenceObjectHint],
) -> ReferenceObjectHint | None:
    hint = by_def.get(record.def_id)
    if hint is not None:
        return hint
    return by_name.get(record.name.lower())


def score_packet(
    total_placements: int,
    object_count: int,
    dll_count: int,
    descriptor_dll_count: int,
    width_count: int,
    record_size_count: int,
    reference_param_count: int,
    referenced_object_count: int,
) -> int:
    score = int((min(total_placements, 5000) ** 0.5) * 80)
    score += max(0, 300 - ((dll_count - 1) * 120))
    score += max(0, 240 - ((object_count - 1) * 20))
    score += min(descriptor_dll_count, 3) * 40
    if width_count == 1:
        score += 120
    elif width_count <= 4:
        score += 60
    if record_size_count == 1:
        score += 40
    if object_count == 1:
        score += 300
    if reference_param_count:
        score += min(reference_param_count, 8) * 12
    score += min(referenced_object_count, 4) * 15
    return score


def build_class_packets(
    records: list[ObjectRecord],
    widths_by_def: dict[int, Counter[int]],
    dll_entries: list[DllEntry],
    reference_class_names: dict[int, str],
    reference_objects_by_def: dict[int, ReferenceObjectHint],
    reference_objects_by_name: dict[str, ReferenceObjectHint],
) -> list[ClassPacket]:
    by_class: dict[int, list[ObjectRecord]] = defaultdict(list)
    for record in records:
        by_class[record.class_id & 0xFFFF].append(record)

    descriptor_dll_ids = {
        entry.dll_id
        for entry in dll_entries
        if entry.status == "descriptor"
    }

    packets: list[ClassPacket] = []
    for class_id, items in by_class.items():
        items.sort(key=lambda item: (-item.placements, item.def_id))
        dll_ids = tuple(sorted({item.dll_id for item in items}))
        used_descriptor_dll_ids = tuple(sorted(dll_id for dll_id in dll_ids if dll_id in descriptor_dll_ids))
        record_sizes = tuple(sorted({item.size for item in items}))
        width_values = tuple(sorted({width for item in items for width in widths_by_def.get(item.def_id, Counter())}))
        map_names = tuple(sorted({item.map_name for item in items if item.map_name is not None}))

        referenced_object_count = 0
        reference_param_count = 0
        for item in items:
            hint = reference_hint_for_record(item, reference_objects_by_def, reference_objects_by_name)
            if hint is None:
                continue
            referenced_object_count += 1
            reference_param_count += len(hint.params)

        class_name = reference_class_names.get(class_id)
        suggested_name = class_name or (items[0].name if len(items) == 1 else f"class_{class_id:04X}")
        output_name = f"class_{class_id:04X}_{slugify(suggested_name)}.c"
        total_placements = sum(item.placements for item in items)
        score = score_packet(
            total_placements=total_placements,
            object_count=len(items),
            dll_count=len(dll_ids),
            descriptor_dll_count=len(used_descriptor_dll_ids),
            width_count=len(width_values),
            record_size_count=len(record_sizes),
            reference_param_count=reference_param_count,
            referenced_object_count=referenced_object_count,
        )

        packets.append(
            ClassPacket(
                class_id=class_id,
                score=score,
                class_name=class_name or "",
                suggested_name=suggested_name,
                output_name=output_name,
                total_placements=total_placements,
                object_count=len(items),
                dll_ids=dll_ids,
                descriptor_dll_ids=used_descriptor_dll_ids,
                record_sizes=record_sizes,
                placement_widths=width_values,
                map_names=map_names,
                objects=tuple(items),
                reference_param_count=reference_param_count,
                referenced_object_count=referenced_object_count,
            )
        )

    packets.sort(
        key=lambda item: (
            -item.score,
            -item.total_placements,
            len(item.dll_ids),
            item.object_count,
            item.class_id,
        )
    )
    return packets


def format_sizes_hex(values: tuple[int, ...]) -> str:
    return ", ".join(f"0x{value:X}" for value in values) if values else "-"


def format_widths(values: tuple[int, ...]) -> str:
    return ", ".join(f"{value}w" for value in values) if values else "-"


def format_dlls(values: tuple[int, ...]) -> str:
    if not values:
        return "-"
    return ", ".join(f"0x{value:04X}" for value in values)


def packet_matches(packet: ClassPacket, pattern: str) -> bool:
    if pattern.startswith("class:"):
        value = pattern[6:]
        return value in (f"{packet.class_id:04x}", f"0x{packet.class_id:04x}")
    if pattern.startswith("dll:"):
        value = pattern[4:]
        return any(value in (f"{dll_id:04x}", f"0x{dll_id:04x}") for dll_id in packet.dll_ids)
    if pattern.startswith("def:"):
        value = pattern[4:]
        return any(value in (f"{item.def_id:04x}", f"0x{item.def_id:04x}") for item in packet.objects)
    if pattern.startswith("map:"):
        value = pattern[4:]
        return any(value in map_name.lower() for map_name in packet.map_names)

    haystacks = [
        f"{packet.class_id:04x}",
        f"0x{packet.class_id:04x}",
        packet.suggested_name.lower(),
        packet.output_name.lower(),
    ]
    if packet.class_name:
        haystacks.append(packet.class_name.lower())
    haystacks.extend(item.name.lower() for item in packet.objects)
    haystacks.extend(map_name.lower() for map_name in packet.map_names)
    haystacks.extend(f"{dll_id:04x}" for dll_id in packet.dll_ids)
    haystacks.extend(f"0x{dll_id:04x}" for dll_id in packet.dll_ids)
    return any(pattern in value for value in haystacks)


def filter_packets(packets: list[ClassPacket], patterns: list[str]) -> list[ClassPacket]:
    if not patterns:
        return packets
    lowered = [pattern.lower() for pattern in patterns]
    return [packet for packet in packets if all(packet_matches(packet, pattern) for pattern in lowered)]


def apply_packet_limits(
    packets: list[ClassPacket],
    max_dlls: int | None,
    max_defs: int | None,
    min_placements: int | None,
) -> list[ClassPacket]:
    limited = packets
    if max_dlls is not None:
        limited = [packet for packet in limited if len(packet.dll_ids) <= max_dlls]
    if max_defs is not None:
        limited = [packet for packet in limited if packet.object_count <= max_defs]
    if min_placements is not None:
        limited = [packet for packet in limited if packet.total_placements >= min_placements]
    return limited


