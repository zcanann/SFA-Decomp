def main(argv: list[str] | None = None) -> int:
    parser = build_arg_parser()
    args = parser.parse_args(argv)

    objindex = load_objindex(args.files_root / "OBJINDEX.bin")
    placements, romlists_by_def = load_romlist_usage(args.files_root, objindex)
    records, _raw_offsets = build_records(args.files_root, placements, romlists_by_def)
    widths_by_def, romlists_by_def_width = load_root_param_widths(args.files_root, objindex)

    symbols = load_symbols(args.symbols)
    table_address = args.dll_table if args.dll_table is not None else guess_dll_table_address(symbols, 0x2C1)
    object_defs, objindex_for_dll = load_object_defs(args.files_root)
    placements_by_dll = load_dll_placement_counts(args.files_root, object_defs, objindex_for_dll)
    objects_by_dll: dict[int, list] = defaultdict(list)
    for obj in object_defs:
        if obj.dll_id != 0xFFFF:
            objects_by_dll[obj.dll_id].append(obj)

    dll_entries = parse_dll_entries(
        dol=DolFile(args.dol),
        table_address=table_address,
        max_dll_id=0x2C1,
        symbols=symbols,
        objects_by_dll=objects_by_dll,
        placements_by_dll=placements_by_dll,
    )
    dll_entries_by_id = {entry.dll_id: entry for entry in dll_entries if entry.status == "descriptor"}

    reference_class_path = resolve_reference_path(args.reference_classes, DEFAULT_REFERENCE_CLASS_CANDIDATES)
    reference_object_path = resolve_reference_path(args.reference_objects, DEFAULT_REFERENCE_OBJECT_CANDIDATES)
    reference_class_names = load_reference_class_names(reference_class_path)
    reference_objects_by_def, reference_objects_by_name = load_reference_object_hints(reference_object_path)

    packets = build_class_packets(
        records,
        widths_by_def,
        dll_entries,
        reference_class_names,
        reference_objects_by_def,
        reference_objects_by_name,
    )
    packets = filter_packets(packets, args.search)
    packets = apply_packet_limits(packets, args.max_dlls, args.max_defs, args.min_placements)

    if args.materialize_top > 0:
        selected = packets[: args.materialize_top]
        written, unchanged, skipped = materialize_packets(
            selected,
            args.output_root,
            widths_by_def,
            dll_entries_by_id,
            reference_objects_by_def,
            reference_objects_by_name,
        )
        print(
            "\n".join(
                [
                    "# Object-class materialization",
                    "",
                    f"- Output root: `{args.output_root.as_posix()}`",
                    f"- Selected packets: `{len(selected)}`",
                    f"- Written: `{len(written)}`",
                    f"- Unchanged: `{len(unchanged)}`",
                    f"- Skipped existing unmanaged files: `{len(skipped)}`",
                ]
            )
        )
        return 0

    if args.search:
        if args.format == "csv":
            sys.stdout.write(rows_to_csv(packets))
        else:
            sys.stdout.write(
                detailed_markdown(
                    packets,
                    widths_by_def,
                    romlists_by_def_width,
                    dll_entries_by_id,
                    reference_objects_by_def,
                    reference_objects_by_name,
                )
            )
            sys.stdout.write("\n")
        return 0

    if args.format == "csv":
        sys.stdout.write(rows_to_csv(packets))
    else:
        sys.stdout.write(
            summary_markdown(
                packets,
                limit=args.limit,
                reference_class_path=reference_class_path,
                reference_object_path=reference_object_path,
            )
        )
        sys.stdout.write("\n")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
