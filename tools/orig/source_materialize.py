def main() -> None:
    parser = build_argument_parser()
    args = parser.parse_args()
    if args.export_direct_artifacts and is_relative_to(args.artifact_output_root.resolve(), args.src_root.resolve()):
        parser.error("Refusing to export direct source artifacts under src/. Use a non-source destination.")

    candidates = collect_candidates(
        retail_strings_path=args.dol,
        retail_symbols_path=args.symbols,
        debug_symbols_path=args.debug_symbols,
        debug_splits_path=args.debug_splits,
        debug_srcfiles_path=args.debug_srcfiles,
    )
    groups = group_candidates(candidates)
    if args.search:
        search_terms = [term.lower() for term in args.search]
        groups = [group for group in groups if matches_search_terms(group, search_terms)]
    source_evidence_by_name = build_source_variant_index(
        collect_all_bundle_source_hits(default_bundle_specs())
    )
    excluded_roots: list[Path] = []
    if args.output_root.resolve() != args.src_root.resolve() and is_relative_to(
        args.output_root.resolve(),
        args.src_root.resolve(),
    ):
        excluded_roots.append(args.output_root)
    legacy_recovered_root = args.src_root / "recovered"
    if legacy_recovered_root.resolve() != args.output_root.resolve():
        excluded_roots.append(legacy_recovered_root)
    legacy_unknown_root = args.src_root / "unknown"
    if legacy_unknown_root.resolve() != args.output_root.resolve():
        excluded_roots.append(legacy_unknown_root)
    existing_basenames = load_existing_source_basenames(
        args.src_root,
        excluded_roots=tuple(excluded_roots),
    )
    stub_decisions, stub_skipped = choose_stub_decisions(
        groups=groups,
        src_root=args.src_root,
        existing_basenames=existing_basenames,
        include_debug_path_only=args.include_debug_path_only,
        include_cross_version_weak=args.include_cross_version_weak,
        source_evidence_by_name=source_evidence_by_name,
    )
    search_terms = [term.lower() for term in args.search]
    gap_window_decisions, gap_window_skipped = choose_gap_window_stub_decisions(
        brief_paths=args.gap_window_brief,
        src_root=args.src_root,
        existing_basenames=existing_basenames,
        search_terms=search_terms,
    )

    artifact_reports = report_artifacts(args.orig_root)
    artifact_reported = [entry for _src_path, _relpath, entry in artifact_reports]
    if args.export_direct_artifacts:
        artifact_written, artifact_unchanged = export_reported_artifacts(
            artifact_reports,
            args.artifact_output_root,
        )
    else:
        artifact_written, artifact_unchanged = [], []
    stub_written, stub_unchanged = materialize_stubs(stub_decisions, args.output_root)
    gap_written, gap_unchanged = materialize_gap_window_stubs(gap_window_decisions, args.output_root)
    manifest = build_manifest(
        artifact_reported=artifact_reported,
        artifact_written=artifact_written,
        artifact_unchanged=artifact_unchanged,
        stub_written=[*stub_written, *gap_written],
        stub_unchanged=[*stub_unchanged, *gap_unchanged],
        stub_skipped=[*stub_skipped, *gap_window_skipped],
    )
    write_manifest(args.manifest, manifest)
    print(
        summary_text(
            manifest,
            args.manifest,
            args.output_root,
            args.artifact_output_root,
            args.export_direct_artifacts,
        )
    )


if __name__ == "__main__":
    main()
