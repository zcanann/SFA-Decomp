SOURCE_SUFFIXES = {".c", ".cpp", ".h", ".hpp"}
GENERIC_FUNC_RE = re.compile(r"(?:^fn_|_func[0-9a-f]+$|fn_[0-9a-f]+$|[A-Za-z]+Fn_[0-9a-f]+$)", re.IGNORECASE)
GENERATED_MARKER = "Auto-generated by tools/orig/source_materialize.py."


@dataclass(frozen=True)
class StubDecision:
    candidate: RecoveryGroup
    output_relpath: str
    confidence: str
    score: int
    reason: str
    bundle_evidence: BundleSourceEvidence | None


def normalize_separators(value: str) -> str:
    return value.replace("\\", "/")


def is_relative_to(path: Path, other: Path) -> bool:
    try:
        path.relative_to(other)
        return True
    except ValueError:
        return False


def load_existing_source_basenames(src_root: Path, excluded_roots: tuple[Path, ...] = ()) -> dict[str, list[str]]:
    result: dict[str, list[str]] = {}
    resolved_excluded = tuple(path.resolve() for path in excluded_roots)
    for path in sorted(src_root.rglob("*")):
        if not path.is_file() or path.suffix.lower() not in SOURCE_SUFFIXES:
            continue
        resolved_path = path.resolve()
        if any(is_relative_to(resolved_path, excluded_root) for excluded_root in resolved_excluded):
            continue
        relpath = path.relative_to(src_root).as_posix()
        result.setdefault(path.name.lower(), []).append(relpath)
    return result


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def copy_file_if_changed(src: Path, dst: Path) -> bool:
    src_bytes = src.read_bytes()
    if dst.is_file() and dst.read_bytes() == src_bytes:
        return False
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copyfile(src, dst)
    return True


def is_managed_generated_stub(path: Path) -> bool:
    if not path.is_file() or path.suffix.lower() not in SOURCE_SUFFIXES:
        return False
    return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="replace")[:512]


def artifact_output_relpath(artifact: DirectSourceArtifact) -> str:
    if artifact.source_tokens:
        token = normalize_separators(artifact.source_tokens[0]).lstrip("/")
        return f"disc_artifacts/{token}"

    relpath = normalize_separators(artifact.relative_path)
    relpath = relpath.removeprefix("files/")
    if relpath.endswith(".new"):
        relpath = relpath[:-4]
    if relpath.endswith(".bak"):
        relpath = relpath[:-4]
    return f"disc_artifacts/{relpath}"


def stub_output_relpath(candidate: RecoveryGroup) -> str:
    basename = Path(candidate.retail_source_name).name
    if candidate.debug_sources:
        return normalize_separators(candidate.debug_sources[0].path)
    return f"unknown/{basename}"


def candidate_confidence(candidate: RecoveryGroup) -> str:
    if candidate.xrefs and candidate.debug_sources:
        return "exact"
    if candidate.xrefs and candidate.debug_symbol_hits:
        return "xref-symbol"
    if candidate.xrefs:
        return "xref-only"
    if candidate.debug_sources:
        return "debug-path-only"
    return "weak"


def candidate_score(candidate: RecoveryGroup) -> int:
    score = 0
    if candidate.xrefs:
        score += 10
    if candidate.debug_sources:
        score += 8
    if candidate.listed_in_debug_srcfiles:
        score += 2
    if candidate.retail_labels:
        score += 2
    score += min(len(candidate.xrefs), 4)
    score += min(len(candidate.debug_symbol_hits), 4)
    return score


def bundle_support_score(evidence: BundleSourceEvidence | None) -> int:
    if evidence is None:
        return 0
    return max(0, len(evidence.all_bundle_ids) - 1) * 2 + min(len(evidence.alias_names), 2)


def choose_stub_decisions(
    groups: list[RecoveryGroup],
    src_root: Path,
    existing_basenames: dict[str, list[str]],
    include_debug_path_only: bool,
    include_cross_version_weak: bool,
    source_evidence_by_name: dict[str, BundleSourceEvidence],
) -> tuple[list[StubDecision], list[dict[str, object]]]:
    generated: list[StubDecision] = []
    skipped: list[dict[str, object]] = []

    for candidate in groups:
        basename = Path(candidate.retail_source_name).name.lower()
        output_relpath = stub_output_relpath(candidate)
        bundle_evidence = source_evidence_by_name.get(candidate.retail_source_name.lower())
        existing_paths = existing_basenames.get(basename, [])
        other_paths = [path for path in existing_paths if path != output_relpath]
        exact_target_exists = output_relpath in existing_paths
        exact_target_is_managed = exact_target_exists and is_managed_generated_stub(src_root / output_relpath)

        if other_paths:
            skipped.append(
                {
                    "type": "stub",
                    "status": "skipped-existing",
                    "retail_source_name": candidate.retail_source_name,
                    "existing_paths": other_paths,
                }
            )
            continue
        if exact_target_exists and not exact_target_is_managed:
            skipped.append(
                {
                    "type": "stub",
                    "status": "skipped-existing",
                    "retail_source_name": candidate.retail_source_name,
                    "existing_paths": [output_relpath],
                }
            )
            continue

        confidence = candidate_confidence(candidate)
        cross_version_strong = bundle_evidence is not None and len(bundle_evidence.all_bundle_ids) >= 3
        if not candidate.xrefs and not (include_debug_path_only and candidate.debug_sources):
            if not (include_cross_version_weak and cross_version_strong):
                skipped.append(
                    {
                        "type": "stub",
                        "status": "skipped-weak",
                        "retail_source_name": candidate.retail_source_name,
                        "confidence": confidence,
                    }
                )
                continue
            reason = "cross-version-source-tag"
        else:
            reason = "retail-xref-backed" if candidate.xrefs else "debug-path-only"

        generated.append(
            StubDecision(
                candidate=candidate,
                output_relpath=output_relpath,
                confidence=confidence,
                score=candidate_score(candidate) + bundle_support_score(bundle_evidence),
                reason=reason,
                bundle_evidence=bundle_evidence,
            )
        )

    generated.sort(key=lambda item: (-item.score, item.output_relpath.lower(), item.candidate.retail_source_name.lower()))
    return generated, skipped


def cleaned_function_hints(candidate: RecoveryGroup, limit: int = 10) -> list[str]:
    result: list[str] = []
    seen: set[str] = set()
    for label in candidate.retail_labels:
        identifier = re.sub(r"[^A-Za-z0-9_]", "_", label).strip("_")
        if not identifier:
            continue
        if identifier[0].isdigit():
            identifier = "_" + identifier
        if identifier in seen:
            continue
        seen.add(identifier)
        result.append(identifier)
        if len(result) >= limit:
            return result
    for name in top_named_functions(candidate.debug_sources, candidate.debug_symbol_hits, limit=limit * 2):
        if GENERIC_FUNC_RE.search(name):
            continue
        if name in seen:
            continue
        seen.add(name)
        result.append(name)
        if len(result) >= limit:
            break
    return result


def format_bundle_hits(evidence: BundleSourceEvidence) -> list[str]:
    lines: list[str] = []
    for hit in evidence.all_hits:
        context = hit.message or hit.label or clean_context_text(hit.text) or hit.text
        lines.append(f" * - {hit.bundle_id} @ 0x{hit.address:08X}: {hit.source_name} :: {context}")
    return lines


def nearby_en_strings(bundle_evidence: BundleSourceEvidence | None) -> list[str]:
    if bundle_evidence is None:
        return []
    en_hit = next((hit for hit in bundle_evidence.all_hits if hit.bundle_id == "GSAE01"), None)
    if en_hit is None:
        return []
    spec_by_id = {spec.bundle_id: spec for spec in default_bundle_specs()}
    en_spec = spec_by_id.get("GSAE01")
    if en_spec is None:
        return []
    return nearby_source_strings(en_spec, en_hit.address)


def render_stub(
    candidate: RecoveryGroup,
    output_relpath: str,
    confidence: str,
    score: int,
    bundle_evidence: BundleSourceEvidence | None,
) -> str:
    function_hints = cleaned_function_hints(candidate)
    lines: list[str] = []
    lines.append("/*")
    lines.append(" * Auto-generated by tools/orig/source_materialize.py.")
    lines.append(" *")
    lines.append(" * This file is intentionally not wired into the build yet.")
    lines.append(" * It exists to turn retail EN source-recovery evidence into a concrete")
    lines.append(" * source-file target that can be filled in during decomp work.")
    lines.append(" *")
    lines.append(f" * Retail source name: {candidate.retail_source_name}")
    lines.append(f" * Output path: {output_relpath}")
    lines.append(f" * Confidence: {confidence}")
    lines.append(f" * Score: {score}")
    lines.append(" *")
    lines.append(" * Retail EN evidence:")
    for address in candidate.retail_addresses:
        lines.append(f" * - main.dol string @ 0x{address:08X}")
    for text in candidate.retail_texts[:6]:
        lines.append(f" * - string: {text}")
    for label in candidate.retail_labels:
        lines.append(f" * - retail label: {label}")
    for message in candidate.retail_messages:
        lines.append(f" * - retail message: {message}")
    if candidate.xrefs:
        for xref in candidate.xrefs[:8]:
            lines.append(f" * - EN xref: 0x{xref.xref_address:08X} {format_function_name(xref)}")
    else:
        lines.append(" * - EN xref: none")
    lines.append(" *")
    lines.append(" * Cross-version bundle evidence:")
    if bundle_evidence is not None:
        for line in format_bundle_hits(bundle_evidence):
            lines.append(line)
        if bundle_evidence.alias_names:
            lines.append(" * - alias source names: " + ", ".join(bundle_evidence.alias_names))
    else:
        lines.append(" * - none")
    lines.append(" *")
    lines.append(" * Retail function-label hints:")
    if candidate.retail_labels:
        for label in candidate.retail_labels:
            lines.append(f" * - retail label: {label}")
    else:
        lines.append(" * - retail label: none")
    lines.append(" *")
    lines.append(" * Debug-side hints only:")
    if candidate.debug_sources:
        for source in candidate.debug_sources[:3]:
            lines.append(
                f" * - path: {source.path} text=0x{source.text_start:08X}-0x{source.text_end:08X}"
            )
    else:
        lines.append(" * - path: none")
    if function_hints:
        for name in function_hints:
            lines.append(f" * - function hint: {name}")
    elif candidate.debug_symbol_hits:
        lines.append(f" * - symbol hint: {candidate.debug_symbol_hits[0]}")
    else:
        lines.append(" * - function hint: none")
    nearby_strings = nearby_en_strings(bundle_evidence)
    if nearby_strings:
        lines.append(" *")
        lines.append(" * Nearby EN strings:")
        for value in nearby_strings[:4]:
            lines.append(f" * - nearby: {value}")
    lines.append(" */")
    lines.append("")

    if function_hints:
        lines.append("#if 0")
        lines.append("/*")
        lines.append(" * Placeholder signatures copied from retail labels and/or debug-side naming only.")
        lines.append(" * Verify each one against EN before promoting it into live code.")
        lines.append(" */")
        for name in function_hints:
            lines.append(f"void {name}(void)")
            lines.append("{")
            lines.append("}")
            lines.append("")
        lines.append("#endif")
    else:
        lines.append("/*")
        lines.append(" * No non-generic function names were strong enough to stub here yet.")
        lines.append(" * Start from the retail strings and EN xrefs above.")
        lines.append(" */")

    return "\n".join(lines).rstrip() + "\n"


def materialize_artifacts(
    orig_root: Path,
    output_root: Path,
) -> tuple[list[dict[str, object]], list[dict[str, object]]]:
    artifacts = collect_direct_source_artifacts(orig_root)
    written: list[dict[str, object]] = []
    skipped: list[dict[str, object]] = []

    for artifact in artifacts:
        src_path = orig_root / artifact.relative_path
        relpath = artifact_output_relpath(artifact)
        dst_path = output_root / relpath
        changed = copy_file_if_changed(src_path, dst_path)
        entry = {
            "type": "artifact",
            "status": "written" if changed else "unchanged",
            "kind": artifact.kind,
            "source_tokens": list(artifact.source_tokens),
            "source_path": artifact.relative_path,
            "output_path": relpath,
        }
        if changed:
            written.append(entry)
        else:
            skipped.append(entry)
    return written, skipped


def materialize_stubs(
    decisions: list[StubDecision],
    output_root: Path,
) -> tuple[list[dict[str, object]], list[dict[str, object]]]:
    written: list[dict[str, object]] = []
    unchanged: list[dict[str, object]] = []

    for decision in decisions:
        text = render_stub(
            candidate=decision.candidate,
            output_relpath=decision.output_relpath,
            confidence=decision.confidence,
            score=decision.score,
            bundle_evidence=decision.bundle_evidence,
        )
        dst_path = output_root / decision.output_relpath
        changed = write_text_if_changed(dst_path, text)
        entry = {
            "type": "stub",
            "status": "written" if changed else "unchanged",
            "retail_source_name": decision.candidate.retail_source_name,
            "confidence": decision.confidence,
            "score": decision.score,
            "reason": decision.reason,
            "output_path": decision.output_relpath,
            "retail_addresses": [f"0x{address:08X}" for address in decision.candidate.retail_addresses],
            "en_xrefs": [format_function_name(xref) for xref in decision.candidate.xrefs],
            "debug_paths": [source.path for source in decision.candidate.debug_sources],
            "function_hints": cleaned_function_hints(decision.candidate),
            "retail_labels": list(decision.candidate.retail_labels),
            "retail_messages": list(decision.candidate.retail_messages),
            "bundle_ids": [] if decision.bundle_evidence is None else list(decision.bundle_evidence.all_bundle_ids),
            "bundle_aliases": [] if decision.bundle_evidence is None else list(decision.bundle_evidence.alias_names),
        }
        if changed:
            written.append(entry)
        else:
            unchanged.append(entry)
    return written, unchanged


def build_manifest(
    artifact_written: list[dict[str, object]],
    artifact_unchanged: list[dict[str, object]],
    stub_written: list[dict[str, object]],
    stub_unchanged: list[dict[str, object]],
    stub_skipped: list[dict[str, object]],
) -> dict[str, object]:
    return {
        "summary": {
            "artifact_written": len(artifact_written),
            "artifact_unchanged": len(artifact_unchanged),
            "stub_written": len(stub_written),
            "stub_unchanged": len(stub_unchanged),
            "stub_skipped": len(stub_skipped),
        },
        "artifacts": {
            "written": artifact_written,
            "unchanged": artifact_unchanged,
        },
        "stubs": {
            "written": stub_written,
            "unchanged": stub_unchanged,
            "skipped": stub_skipped,
        },
    }


def write_manifest(path: Path, manifest: dict[str, object]) -> bool:
    text = json.dumps(manifest, indent=2, sort_keys=True) + "\n"
    return write_text_if_changed(path, text)


def summary_text(manifest: dict[str, object], manifest_path: Path, output_root: Path) -> str:
    summary = manifest["summary"]
    lines = [
        "# Source materialization",
        "",
        f"- Output root: `{output_root.as_posix()}`",
        f"- Artifact files written: `{summary['artifact_written']}`",
        f"- Artifact files unchanged: `{summary['artifact_unchanged']}`",
        f"- Stub files written: `{summary['stub_written']}`",
        f"- Stub files unchanged: `{summary['stub_unchanged']}`",
        f"- Stub candidates skipped: `{summary['stub_skipped']}`",
        f"- Manifest: `{manifest_path.as_posix()}`",
    ]
    return "\n".join(lines)


def build_argument_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Materialize retail orig/ source artifacts and EN source-recovery stubs into src/."
    )
    parser.add_argument(
        "--orig-root",
        type=Path,
        default=Path("orig/GSAE01"),
        help="Path to the EN orig bundle root.",
    )
    parser.add_argument(
        "--dol",
        type=Path,
        default=Path("orig/GSAE01/sys/main.dol"),
        help="Path to the retail EN main.dol.",
    )
    parser.add_argument(
        "--symbols",
        type=Path,
        default=Path("config/GSAE01/symbols.txt"),
        help="Current EN symbols.txt for naming retail xref callsites.",
    )
    parser.add_argument(
        "--debug-symbols",
        type=Path,
        default=Path("reference_projects/rena-tools/sfadebug/config/GSAP01-DEBUG/symbols.txt"),
        help="Debug-side symbols used only as side evidence.",
    )
    parser.add_argument(
        "--debug-splits",
        type=Path,
        default=Path("reference_projects/rena-tools/sfadebug/config/GSAP01-DEBUG/splits.txt"),
        help="Debug-side splits used only as side evidence.",
    )
    parser.add_argument(
        "--debug-srcfiles",
        type=Path,
        default=Path("reference_projects/rena-tools/sfadebug/notes/srcfiles.txt"),
        help="Debug-side source filename inventory used only as side evidence.",
    )
    parser.add_argument(
        "--src-root",
        type=Path,
        default=Path("src"),
        help="Active source tree used to skip basenames that already exist.",
    )
    parser.add_argument(
        "--output-root",
        type=Path,
        default=Path("src"),
        help="Destination root for copied artifacts and generated stubs.",
    )
    parser.add_argument(
        "--manifest",
        type=Path,
        default=Path("docs/orig/source_materialize.json"),
        help="JSON manifest describing what was written and why.",
    )
    parser.add_argument(
        "--include-debug-path-only",
        action="store_true",
        help="Also emit stubs for debug-path-only candidates that lack an EN xref.",
    )
    parser.add_argument(
        "--include-cross-version-weak",
        action="store_true",
        help="Also emit weak EN candidates that are repeated across multiple bundled retail versions.",
    )
    return parser


def main() -> None:
    parser = build_argument_parser()
    args = parser.parse_args()

    candidates = collect_candidates(
        retail_strings_path=args.dol,
        retail_symbols_path=args.symbols,
        debug_symbols_path=args.debug_symbols,
        debug_splits_path=args.debug_splits,
        debug_srcfiles_path=args.debug_srcfiles,
    )
    groups = group_candidates(candidates)
    source_evidence_by_name = build_source_variant_index(
        collect_all_bundle_source_hits(default_bundle_specs())
    )
    excluded_roots: list[Path] = []
    if args.output_root.resolve() != args.src_root.resolve() and is_relative_to(
        args.output_root.resolve(),
        args.src_root.resolve(),
    ):
        excluded_roots.append(args.output_root)
    legacy_recovered_root = args.src_root / "recovered"
    if legacy_recovered_root.resolve() != args.output_root.resolve():
        excluded_roots.append(legacy_recovered_root)
    existing_basenames = load_existing_source_basenames(
        args.src_root,
        excluded_roots=tuple(excluded_roots),
    )
    stub_decisions, stub_skipped = choose_stub_decisions(
        groups=groups,
        src_root=args.src_root,
        existing_basenames=existing_basenames,
        include_debug_path_only=args.include_debug_path_only,
        include_cross_version_weak=args.include_cross_version_weak,
        source_evidence_by_name=source_evidence_by_name,
    )

    artifact_written, artifact_unchanged = materialize_artifacts(args.orig_root, args.output_root)
    stub_written, stub_unchanged = materialize_stubs(stub_decisions, args.output_root)
    manifest = build_manifest(
        artifact_written=artifact_written,
        artifact_unchanged=artifact_unchanged,
        stub_written=stub_written,
        stub_unchanged=stub_unchanged,
        stub_skipped=stub_skipped,
    )
    write_manifest(args.manifest, manifest)
    print(summary_text(manifest, args.manifest, args.output_root))


if __name__ == "__main__":
    main()
