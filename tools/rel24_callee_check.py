"""REL24 callee screen v2.

For every `bl` in .text of the retail carved object and of our source object,
determine the callee NAME. Retail carved objects sometimes leave a cross-object
call as a raw PC-relative offset with no relocation; resolve those through the
unit's .text link base (splits.txt) + symbols.txt.
"""
import json, os, re, sys, struct
from elftools.elf.elffile import ELFFile
from elftools.elf.sections import SymbolTableSection
from elftools.elf.relocation import RelocationSection

R_PPC_REL24 = 10


def parse_symbols(path):
    out = {}
    pat = re.compile(r'^\s*([A-Za-z_$][\w$]*)\s*=\s*\.?\w*:?\s*(0x[0-9A-Fa-f]+)')
    for line in open(path):
        m = pat.match(line)
        if m:
            out[int(m.group(2), 16)] = m.group(1)
    return out


def parse_splits(path):
    """unit source path -> .text start address"""
    out = {}
    cur = None
    for line in open(path):
        s = line.strip()
        if not s or s.startswith('#'):
            continue
        if not line[0].isspace() and s.endswith(':'):
            cur = s[:-1]
            continue
        m = re.match(r'\.text\s+start:(0x[0-9A-Fa-f]+)', s)
        if m and cur and cur not in out:
            out[cur] = int(m.group(1), 16)
    return out


def bls(path, textbase, addr2name):
    """-> {func_name: [(off_in_fn, callee, resolved_how)]}"""
    with open(path, 'rb') as f:
        ef = ELFFile(f)
        symtab = [s for s in ef.iter_sections() if isinstance(s, SymbolTableSection)][0]
        syms = list(symtab.iter_symbols())
        relocs = {}   # (secidx, off) -> name
        for s in ef.iter_sections():
            if not isinstance(s, RelocationSection):
                continue
            for r in s.iter_relocations():
                if r['r_info_type'] == R_PPC_REL24:
                    relocs[(s.header['sh_info'], r['r_offset'])] = syms[r['r_info_sym']].name
        funcs = {}
        for sym in syms:
            if sym['st_info']['type'] == 'STT_FUNC' and isinstance(sym['st_shndx'], int):
                funcs.setdefault(sym['st_shndx'], []).append(
                    (sym['st_value'], sym['st_size'], sym.name))
        for k in funcs:
            funcs[k].sort()
        out = {}
        for si, sec in enumerate(ef.iter_sections()):
            if not sec.name.startswith('.text'):
                continue
            data = sec.data()
            for (v, sz, name) in funcs.get(si, []):
                lst = []
                for off in range(v, v + sz, 4):
                    if off + 4 > len(data):
                        break
                    w = struct.unpack('>I', data[off:off + 4])[0]
                    if (w >> 26) != 18 or (w & 3) != 1:   # bl only (LK set, AA clear)
                        continue
                    r = relocs.get((si, off))
                    if r is not None:
                        lst.append((off - v, r, 'reloc'))
                    elif textbase is not None:
                        li = w & 0x03FFFFFC
                        if li & 0x02000000:
                            li -= 0x04000000
                        tgt = (textbase + off + li) & 0xFFFFFFFF
                        lst.append((off - v, addr2name.get(tgt, 'ADDR:%08X' % tgt), 'addr'))
                    else:
                        lst.append((off - v, 'UNRESOLVED', 'none'))
                out[name] = lst
        return out


syms = parse_symbols('config/GSAE01/symbols.txt')
splits = parse_splits('config/GSAE01/splits.txt')
cfg = json.load(open('build/GSAE01/config.json'))
rep = json.load(open('build/GSAE01/report.json'))
fz = {}
for u in rep['units']:
    for fn in u.get('functions', []) or []:
        if 'fuzzy_match_percent' in fn:
            fz.setdefault(fn['name'], []).append((u['name'], fn['fuzzy_match_percent'], fn['size']))

stats = dict(reloc=0, addr=0, none=0, unknown=0, fns=0)
mism = []
for u in cfg['units']:
    if u.get('autogenerated'):
        continue
    tpath = u['object']
    spath = tpath.replace('build/GSAE01/obj/', 'build/GSAE01/src/')
    srcname = tpath.replace('build/GSAE01/obj/', '').rsplit('.o', 1)[0] + '.c'
    base = splits.get(srcname)
    T = bls(tpath, base, syms)
    S = bls(spath, base, syms)
    for fn, tl in T.items():
        for _, n, how in tl:
            stats[how] += 1
            if n.startswith('ADDR:'):
                stats['unknown'] += 1
        if fn not in S:
            continue
        stats['fns'] += 1
        tn = [n for _, n, _ in tl]
        sn = [n for _, n, _ in S[fn]]
        if tn != sn:
            mism.append(dict(unit=srcname, fn=fn, retail=tn, ours=sn, fuzzy=fz.get(fn)))

print('splits parsed:', len(splits), 'symbols:', len(syms))
print('retail bl sites: by reloc=%d by addr=%d unresolvable=%d (of which unknown addr=%d)'
      % (stats['reloc'], stats['addr'], stats['none'], stats['unknown']))
print('functions compared:', stats['fns'])
print('MISMATCHES:', len(mism))
json.dump(mism, open(sys.argv[1], 'w'), indent=1)
